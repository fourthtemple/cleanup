export function installPoseTimelineMethods(BirdWeightEditor, deps) {
  const {
    THREE,
    OrbitControls,
    TransformControls,
    cloneClipWithStartOffsetApplied,
    configuredClipStartOffsetSeconds,
    remainingClipStartOffsetSeconds,
    loadBirdFlapProfile,
    ACTOR_TARGETS,
    PREVIEW_PARAMS,
    BASE_COLOR,
    SELECTED_COLOR,
    MODIFIED_COLOR,
    SELECTED_MODIFIED_COLOR,
    CURVE_CHANNELS,
    CURVE_CHANNEL_KEYS,
    ADDITIVE_POSE_EASE_FRAMES,
    RIG_BONE_GROUPS,
    EDIT_ONLY_TOOLS,
    finitePoseValue,
    writeJsonFile
  } = deps;
  const POSE_CHANNELS = ["x", "y", "z", "px", "py", "pz"];
  const ROTATION_CHANNELS = new Set(["x", "y", "z"]);
  Object.assign(BirdWeightEditor.prototype, {
    markPoseKeyframesAuthored() {
      // Generated solver curves are blue guide keys. Any user-authored solved,
      // adaptive, or additive edit is red.
      this.poseKeyframesGenerated = false;
      this.timelineKeysSourceWasAutoGenerated = false;
      this.syncTimelineSourceControl?.();
    },

    setPoseKeyframeKind(frame, kind = "authored") {
      this.poseKeyframeKinds ||= new Map();
      const targetFrame = Math.max(0, Math.min(this.timelineFrames, Math.round(frame)));
      if (kind === "ik") {
        this.poseKeyframeKinds.set(targetFrame, "ik");
      } else {
        this.poseKeyframeKinds.delete(targetFrame);
      }
    },

    poseKeyframeKindForEdit(keyedNames = []) {
      return this.activePoseGizmoMode?.() === "ik" || keyedNames.length > 1 ? "ik" : "authored";
    },

    poseKeyframeMarkerType(frame, framePose = {}) {
      if (this.poseKeyframeKinds?.get?.(frame) === "ik") {
        return "ik";
      }
      const keyedCount = Object.keys(framePose || {}).filter((name) => this.bones?.has?.(name)).length;
      return keyedCount > 1 ? "ik" : "authored";
    },

    updateManualPoseFromControls(options = {}) {
      const boneName = this.poseBoneSelect.value;
      if (!boneName || !this.bones.has(boneName)) {
        return;
      }
      const visibleFrame = Number(this.timelineScrub?.value);
      if (Number.isFinite(visibleFrame)) {
        this.progress = THREE.MathUtils.clamp(visibleFrame / this.timelineFrames, 0, 1);
        this.timeScrub.value = String(this.progress);
        this.syncTimelineControls();
      }
      const editedChannel = typeof options.channel === "string" ? options.channel : "";
      const pose = this.readPoseControls();
      const currentManualPose = this.manualPose.get(boneName) || {};
      if (!this.manualPoseEditedChannels) {
        this.manualPoseEditedChannels = new Map();
      }
      let editedChannels = this.manualPoseEditedChannels.get(boneName);
      if (!editedChannels) {
        editedChannels = new Set(Object.keys(currentManualPose).filter((channel) => ROTATION_CHANNELS.has(channel)));
      }
      const nextPose = {};
      if (editedChannel) {
        editedChannels.add(editedChannel);
        for (const channel of POSE_CHANNELS) {
          if (editedChannels.has(channel) && currentManualPose[channel] !== undefined) {
            nextPose[channel] = finitePoseValue(currentManualPose[channel]);
          }
        }
        nextPose[editedChannel] = finitePoseValue(pose[editedChannel]);
      } else {
        for (const channel of POSE_CHANNELS) {
          nextPose[channel] = finitePoseValue(pose[channel]);
          if (Math.abs(nextPose[channel]) >= 0.0001) {
            editedChannels.add(channel);
          }
        }
      }
      this.manualPoseEditedChannels.set(boneName, editedChannels);
      const constrainedPose = this.clampPoseWithJointConstraint?.(boneName, nextPose) || nextPose;
      if (constrainedPose !== nextPose) {
        this.setPoseControlsFromPose(constrainedPose, boneName);
      }
      const isZeroPose = POSE_CHANNELS
        .reduce((sum, channel) => sum + Math.abs(finitePoseValue(constrainedPose[channel])), 0) < 0.0001;
      for (const [name, nextPose] of this.mirroredBoneEntries(boneName, constrainedPose)) {
        if (isZeroPose) {
          this.manualPose.delete(name);
          this.manualPoseAdditiveNames?.delete?.(name);
          this.manualPoseEditedChannels.delete(name);
        } else {
          this.manualPose.set(name, nextPose);
          this.markManualPoseStorageMode?.(name);
          this.manualPoseEditedChannels.set(name, new Set(editedChannels));
        }
      }
      this.updatePoseOutputs();
      this.applyPose(this.progress);
      this.flushPoseUpdates();
      this.syncPatchJson();
      if (!options.silent && this.mirrorMode && this.isMirrorableBone(boneName)) {
        this.setStatus(`Mirrored ${boneName} to ${this.mirrorBoneName(boneName)}`);
      }
    },

    discardUnkeyedPosePreview(options = {}) {
      if (!this.manualPose?.size) {
        return false;
      }
      const {
        applyPose = true,
        syncControls = true,
        status = false
      } = options;
      this.manualPose.clear();
      this.manualPoseAdditiveNames?.clear?.();
      this.manualPoseEditedChannels?.clear?.();
      if (applyPose) {
        this.applyPose(this.progress);
      }
      if (syncControls) {
        this.syncPoseControlsToCurrentBone();
      }
      this.updateBoneLayerValues();
      this.updateCounts();
      if (status) {
        this.setStatus("Ignored unkeyed pose edits");
      }
      return true;
    },

    readPoseControls() {
      return {
        x: finitePoseValue(this.poseRotX.value),
        y: finitePoseValue(this.poseRotY.value),
        z: finitePoseValue(this.poseRotZ.value),
        px: finitePoseValue(this.posePosX.value),
        py: finitePoseValue(this.posePosY.value),
        pz: finitePoseValue(this.posePosZ.value)
      };
    },

    flushPoseUpdates() {
      if (!this.model) {
        return;
      }
      this.model.updateMatrixWorld(true);
      for (const record of this.paintRecords) {
        record.object.skeleton?.update?.();
      }
      this.updateSelectedBoneHighlight();
      this.updateBonePickerOverlay();
      this.updateBoneLayerValues();
      if (this.showBonesLayer) {
        this.updateBoneLabels();
      }
    },

    setPoseControlReadout(readout, value, decimals) {
      if (!readout) {
        return;
      }
      const text = Number(value).toFixed(decimals);
      if (readout.tagName === "INPUT") {
        readout.value = text;
        return;
      }
      readout.textContent = text;
    },

    poseControlFields() {
      return [
        { channel: "x", range: this.poseRotX, input: this.poseRotXValue, defaultMin: -1.57, defaultMax: 1.57, step: 0.01, decimals: 2 },
        { channel: "y", range: this.poseRotY, input: this.poseRotYValue, defaultMin: -1.57, defaultMax: 1.57, step: 0.01, decimals: 2 },
        { channel: "z", range: this.poseRotZ, input: this.poseRotZValue, defaultMin: -1.57, defaultMax: 1.57, step: 0.01, decimals: 2 },
        { channel: "px", range: this.posePosX, input: this.posePosXValue, defaultMin: -30, defaultMax: 30, step: 0.005, decimals: 3 },
        { channel: "py", range: this.posePosY, input: this.posePosYValue, defaultMin: -30, defaultMax: 30, step: 0.005, decimals: 3 },
        { channel: "pz", range: this.posePosZ, input: this.posePosZValue, defaultMin: -30, defaultMax: 30, step: 0.005, decimals: 3 }
      ];
    },

    poseControlField(channel) {
      return this.poseControlFields().find((field) => field.channel === channel) || null;
    },

    poseControlDomainFor(channel, boneName, currentValue = 0) {
      const field = this.poseControlField(channel);
      if (!field) {
        return { min: -1, max: 1 };
      }
      const values = [0, field.defaultMin, field.defaultMax];
      const addValue = (value) => {
        const number = Number(value);
        if (Number.isFinite(number)) {
          values.push(number);
        }
      };
      addValue(currentValue);
      if (boneName) {
        for (const framePose of this.poseKeyframes.values()) {
          addValue(framePose?.[boneName]?.[channel]);
        }
        addValue(this.manualPose.get(boneName)?.[channel]);
        const mirrorName = this.mirrorMode ? this.mirrorBoneName(boneName) : "";
        if (mirrorName) {
          for (const framePose of this.poseKeyframes.values()) {
            const mirrorPose = framePose?.[mirrorName] ? this.mirrorPose(framePose[mirrorName]) : null;
            addValue(mirrorPose?.[channel]);
          }
          const mirroredManualPose = this.manualPose.get(mirrorName) ? this.mirrorPose(this.manualPose.get(mirrorName)) : null;
          addValue(mirroredManualPose?.[channel]);
        }
      }
      const maxMagnitude = Math.max(...values.map((value) => Math.abs(value)));
      const defaultMagnitude = Math.max(Math.abs(field.defaultMin), Math.abs(field.defaultMax));
      if (maxMagnitude <= defaultMagnitude) {
        return { min: field.defaultMin, max: field.defaultMax };
      }
      const increment = defaultMagnitude >= 30 ? 5 : 0.25;
      const limit = Math.ceil((maxMagnitude * 1.08) / increment) * increment;
      return { min: -limit, max: limit };
    },

    setPoseControlDomain(channel, domain) {
      const field = this.poseControlField(channel);
      if (!field || !domain) {
        return;
      }
      const min = Number(domain.min);
      const max = Number(domain.max);
      if (!Number.isFinite(min) || !Number.isFinite(max) || min >= max) {
        return;
      }
      const minText = String(Number(min.toFixed(field.decimals)));
      const maxText = String(Number(max.toFixed(field.decimals)));
      if (field.range) {
        if (field.range.min !== minText) {
          field.range.min = minText;
        }
        if (field.range.max !== maxText) {
          field.range.max = maxText;
        }
      }
    },

    updatePoseControlDomains(pose = {}, boneName = this.poseBoneSelect?.value) {
      for (const field of this.poseControlFields()) {
        this.setPoseControlDomain(
          field.channel,
          this.poseControlDomainFor(field.channel, boneName, pose[field.channel])
        );
      }
    },

    expandPoseControlDomainForValue(channel, value, boneName = this.poseBoneSelect?.value) {
      this.setPoseControlDomain(channel, this.poseControlDomainFor(channel, boneName, value));
    },

    setPoseControlsFromPose(pose = {}, boneName = this.poseBoneSelect?.value) {
      const fullPose = {
        x: pose.x || 0,
        y: pose.y || 0,
        z: pose.z || 0,
        px: pose.px || 0,
        py: pose.py || 0,
        pz: pose.pz || 0
      };
      this.updatePoseControlDomains(fullPose, boneName);
      this.poseRotX.value = String(fullPose.x);
      this.poseRotY.value = String(fullPose.y);
      this.poseRotZ.value = String(fullPose.z);
      this.posePosX.value = String(fullPose.px);
      this.posePosY.value = String(fullPose.py);
      this.posePosZ.value = String(fullPose.pz);
      this.updatePoseOutputs();
    },

    updatePoseOutputs() {
      this.setPoseControlReadout(this.poseRotXValue, this.poseRotX.value, 2);
      this.setPoseControlReadout(this.poseRotYValue, this.poseRotY.value, 2);
      this.setPoseControlReadout(this.poseRotZValue, this.poseRotZ.value, 2);
      this.setPoseControlReadout(this.posePosXValue, this.posePosX.value, 3);
      this.setPoseControlReadout(this.posePosYValue, this.posePosY.value, 3);
      this.setPoseControlReadout(this.posePosZValue, this.posePosZ.value, 3);
    },

    syncPoseControls() {
      const boneName = this.poseBoneSelect.value;
      if (!boneName) {
        this.setPoseControlsFromPose({}, "");
        return;
      }
      const framePose = this.getPoseAtFrame(this.currentFrame(), boneName);
      const interpolatedPose = this.interpolatedPoseForFrame(this.currentFrame());
      const keyedPose = framePose
        ? { ...(interpolatedPose[boneName] || {}), ...framePose }
        : interpolatedPose[boneName];
      const mirrorName = this.mirrorBoneName(boneName);
      const mirroredFramePose = this.mirrorMode && mirrorName ? this.getPoseAtFrame(this.currentFrame(), mirrorName) : null;
      const mirroredManualPose = this.mirrorMode && mirrorName ? this.manualPose.get(mirrorName) : null;
      const mirroredKeyedPose = mirroredFramePose
        ? { ...(interpolatedPose[mirrorName] || {}), ...mirroredFramePose }
        : interpolatedPose[mirrorName];
      const pose = this.manualPose.get(boneName)
        || keyedPose
        || (mirroredManualPose ? this.mirrorPose(mirroredManualPose) : null)
        || (this.mirrorMode && mirrorName && mirroredKeyedPose ? this.mirrorPose(mirroredKeyedPose) : null)
        || { x: 0, y: 0, z: 0 };
      this.setPoseControlsFromPose(pose, boneName);
    },

    syncPoseControlsToCurrentBone() {
      if (this.draggingPoseControl) {
        return;
      }
      const boneName = this.poseBoneSelect.value;
      if (!boneName || !this.bones.has(boneName)) {
        this.setPoseControlsFromPose({}, "");
        return;
      }
      if (this.actorTarget?.mode !== "bird-flap") {
        const framePose = this.getPoseAtFrame(this.currentFrame(), boneName);
        const interpolatedPose = this.interpolatedPoseForFrame(this.currentFrame());
        const keyedPose = framePose
          ? { ...(interpolatedPose[boneName] || {}), ...framePose }
          : interpolatedPose[boneName];
        const mirrorName = this.mirrorBoneName(boneName);
        const mirroredFramePose = this.mirrorMode && mirrorName ? this.getPoseAtFrame(this.currentFrame(), mirrorName) : null;
        const mirroredManualPose = this.mirrorMode && mirrorName ? this.manualPose.get(mirrorName) : null;
        const mirroredKeyedPose = mirroredFramePose
          ? { ...(interpolatedPose[mirrorName] || {}), ...mirroredFramePose }
          : interpolatedPose[mirrorName];
        const pose = this.manualPose.get(boneName)
          || keyedPose
          || (mirroredManualPose ? this.mirrorPose(mirroredManualPose) : null)
          || (this.mirrorMode && mirrorName && mirroredKeyedPose ? this.mirrorPose(mirroredKeyedPose) : null)
          || { x: 0, y: 0, z: 0, px: 0, py: 0, pz: 0 };
        this.setPoseControlsFromPose(pose, boneName);
        return;
      }
      const pose = this.getBoneRelativePose(boneName);
      this.setPoseControlsFromPose(pose, boneName);
    },

    poseEditChainCandidates(boneName = this.poseBoneSelect?.value) {
      if (!boneName || !this.bones.has(boneName)) {
        return [];
      }
      const chains = [];
      const pushChain = (names = []) => {
        const clean = [...new Set(names)].filter((name) => this.bones.has(name));
        if (clean.length >= 2 && clean.includes(boneName) && !chains.some((chain) => chain.join("|") === clean.join("|"))) {
          chains.push(clean);
        }
      };
      pushChain(this.selectedBoneChainNames?.() || []);
      for (const chain of this.customBoneChains?.() || []) {
        pushChain(chain.names || []);
      }
      return chains;
    },

    poseEditTargetNames(options = {}) {
      const boneName = options.boneName || this.poseBoneSelect?.value;
      if (!boneName || !this.bones.has(boneName)) {
        return [];
      }
      const frame = Number.isFinite(options.frame) ? options.frame : null;
      const framePose = frame !== null ? this.poseKeyframes.get(frame) : null;
      const targets = new Set([boneName]);
      for (const chain of this.poseEditChainCandidates(boneName)) {
        const manualCount = chain.filter((name) => this.manualPose.has(name)).length;
        const keyedCount = chain.filter((name) => framePose?.[name]).length;
        if (this.activePoseGizmoMode?.() === "ik" || manualCount > 1 || keyedCount > 1) {
          chain.forEach((name) => targets.add(name));
          break;
        }
      }

      const mirrored = [];
      for (const name of targets) {
        for (const [targetName] of this.mirroredBoneEntries(name, { x: 0, y: 0, z: 0, px: 0, py: 0, pz: 0 })) {
          if (this.bones.has(targetName) && !mirrored.includes(targetName)) {
            mirrored.push(targetName);
          }
        }
      }
      return mirrored;
    },

    clearCurrentPose() {
      const boneName = this.poseBoneSelect.value;
      if (!boneName) {
        return;
      }
      const targets = this.poseEditTargetNames({ boneName });
      const cleared = targets.filter((name) => this.manualPose.has(name));
      for (const name of targets) {
        this.manualPose.delete(name);
        this.manualPoseAdditiveNames?.delete?.(name);
        this.manualPoseEditedChannels?.delete?.(name);
      }
      this.setPoseControlsFromPose({ x: 0, y: 0, z: 0, px: 0, py: 0, pz: 0 }, boneName);
      this.applyPose(this.progress);
      this.flushPoseUpdates?.();
      this.syncPatchJson();
      const label = targets.length > 1
        ? `${targets.length}-bone chain`
        : this.boneDisplayName(boneName);
      this.setStatus(cleared.length ? `Cleared ${label} pose` : `No unkeyed ${label} pose to clear`);
    },

    keyCurrentPose() {
      this.stopSequencePreview?.({ applyPose: false, resetElapsed: true });
      this.pausePlayback?.();
      const frame = this.currentFrame();
      const boneName = this.poseBoneSelect.value;
      if (!boneName) {
        return;
      }
      const useAdditiveKinematics = this.canUseAdditiveKinematicsForCurrentLayer?.() === true;
      if (useAdditiveKinematics) {
        this.prepareAdditiveKinematicsLayerForEdit?.();
      }
      const useAdaptiveEdit = this.canUseAdaptiveEditForCurrentLayer?.() === true;
      const adaptiveAbsoluteEdit = this.shouldConvertSolvedEditToAdaptive?.() === true;
      if (!this.manualPose.has(boneName)) {
        const pose = this.readPoseControls();
        for (const [name, nextPose] of this.mirroredBoneEntries(boneName, pose)) {
          this.manualPose.set(name, nextPose);
          this.markManualPoseStorageMode?.(name);
        }
      }
      const keyedNames = useAdaptiveEdit
        ? this.poseEditTargetNames({ boneName, frame })
        : this.activePoseBoneNames();
      const adaptiveGuideAnchors = useAdaptiveEdit
        ? this.captureAdaptiveEditGuideAnchors?.(frame, keyedNames, CURVE_CHANNEL_KEYS)
        : null;
      const preConversionPose = adaptiveAbsoluteEdit
        ? this.interpolatedPoseForFrame(frame)
        : null;
      if (useAdaptiveEdit) {
        this.prepareAdaptivePoseLayerForEdit?.();
      }
      this.markPoseKeyframesAuthored();
      this.applyAdaptiveEditGuideAnchors?.(adaptiveGuideAnchors);
      const framePose = this.poseKeyframes.get(frame) || {};
      if (this.actorTarget?.mode !== "bird-flap" && this.poseKeyframeMode !== "replace") {
        const interpolatedPose = this.interpolatedPoseForFrame(frame);
        for (const keyedName of keyedNames) {
          const manualPose = this.manualPose.get(keyedName);
          const manualPoseIsAdditive = manualPose && this.manualPoseAdditiveNames?.has?.(keyedName);
          const sourcePose = this.clonePose(
            manualPose
              || preConversionPose?.[keyedName]
              || interpolatedPose[keyedName]
              || framePose[keyedName]
              || { x: 0, y: 0, z: 0, px: 0, py: 0, pz: 0 }
          );
          framePose[keyedName] = adaptiveAbsoluteEdit && !manualPoseIsAdditive
            ? this.adaptivePoseFromAbsolutePose(frame, keyedName, sourcePose)
            : sourcePose;
          this.ensureAdditivePoseAnchors?.(keyedName, frame, CURVE_CHANNEL_KEYS);
        }
      } else {
        this.applyPose(this.progress);
        const interpolatedPose = this.interpolatedPoseForFrame(frame);
        for (const keyedName of keyedNames) {
          framePose[keyedName] = this.clonePose(
            this.manualPose.get(keyedName)
              || interpolatedPose[keyedName]
              || this.getBoneRelativePose(keyedName)
          );
        }
      }
      this.poseKeyframes.set(frame, framePose);
      this.setPoseKeyframeKind(frame, this.poseKeyframeKindForEdit(keyedNames));
      keyedNames.forEach((keyedName) => {
        this.manualPose.delete(keyedName);
        this.manualPoseAdditiveNames?.delete?.(keyedName);
        this.manualPoseEditedChannels?.delete?.(keyedName);
      });
      this.applyPose(this.progress);
      this.syncPoseControlsToCurrentBone();
      this.syncPatchJson();
      this.updateTimelineKeyMarkers();
      this.updateCounts();
      this.setStatus(`Keyed ${keyedNames.length} ${keyedNames.length === 1 ? "bone" : "bones"} at frame ${frame}`);
    },

    activePoseBoneNames() {
      const names = new Set(this.manualPose.keys());
      const selected = this.poseBoneSelect.value;
      if (selected) {
        names.add(selected);
        const mirrorName = this.mirrorMode ? this.mirrorBoneName(selected) : "";
        if (mirrorName) {
          names.add(mirrorName);
        }
      }
      if (this.actorTarget?.mode === "bird-flap" || this.poseKeyframeMode === "replace") {
        for (const framePose of this.poseKeyframes.values()) {
          Object.keys(framePose || {}).forEach((name) => names.add(name));
        }
      }
      return [...names].filter((name) => this.bones.has(name));
    },

    deleteCurrentKey() {
      const frame = this.currentFrame();
      const boneName = this.poseBoneSelect.value;
      if (!boneName) {
        return;
      }
      const framePose = this.poseKeyframes.get(frame);
      const deleted = Object.keys(framePose || {});
      const frameIsGeneratedGuide = this.poseKeyframesGenerated
        || (this.poseKeyframeMode === "replace" && this.timelineKeysSourceWasAutoGenerated);
      if (!deleted.length || (this.shouldUseAdaptiveEdits?.() && frameIsGeneratedGuide)) {
        const guideTargetNames = [...(this.timelineKeyMarkerBoneNames?.() || [])];
        const promotedGuide = this.promoteAdaptiveGuidePointToStatic?.(
          frame,
          guideTargetNames.length ? guideTargetNames : [boneName],
          CURVE_CHANNEL_KEYS
        );
        if (promotedGuide) {
          this.syncPatchJson();
          this.updateTimelineKeyMarkers();
          this.updateCounts();
          this.applyPose(this.progress);
          this.syncPoseControlsToCurrentBone();
          this.setStatus(
            `Deleted frame ${frame} adaptive guide (${promotedGuide.anchors} red neighbor ${promotedGuide.anchors === 1 ? "point" : "points"})`
          );
          return;
        }
      }
      if (!deleted.length) {
        this.setStatus("No key on this frame");
        return;
      }
      this.markPoseKeyframesAuthored();
      this.poseKeyframes.delete(frame);
      this.poseKeyframeKinds?.delete?.(frame);
      deleted.forEach((name) => {
        this.deleteAdaptiveGuidePoint?.(frame, name, CURVE_CHANNEL_KEYS);
        this.manualPose.delete(name);
        this.manualPoseAdditiveNames?.delete?.(name);
        this.manualPoseEditedChannels?.delete?.(name);
      });
      this.syncPatchJson();
      this.updateTimelineKeyMarkers();
      this.updateCounts();
      this.applyPose(this.progress);
      this.syncPoseControlsToCurrentBone();
      this.setStatus(`Deleted frame ${frame} key (${deleted.length} ${deleted.length === 1 ? "bone" : "bones"})`);
    },

    clearKeyframes() {
      this.poseKeyframes.clear();
      this.poseCurveHandles?.clear?.();
      this.poseKeyframeKinds?.clear?.();
      this.manualPose.clear();
      this.manualPoseAdditiveNames?.clear?.();
      this.manualPoseEditedChannels?.clear?.();
      this.adaptiveGuideKeyframes = new Map();
      this.adaptiveGuideDeltaKeyframes = new Map();
      this.adaptiveGuideCurveHandles = new Map();
      this.poseKeyframeMode = "additive";
      this.poseKeyframesGenerated = false;
      this.timelineKeysSourceWasAutoGenerated = false;
      this.syncPatchJson();
      this.updateTimelineKeyMarkers();
      this.updateCounts();
      this.syncPoseControls();
      this.applyPose(this.progress);
      this.setStatus("Cleared pose keys");
    },

    goToAdjacentKey(direction) {
      this.stopSequencePreview?.({ applyPose: false, resetElapsed: true });
      this.pausePlayback();
      this.discardUnkeyedPosePreview({ applyPose: false, syncControls: false });
      const current = this.currentFrame();
      const next = direction > 0
        ? (current >= this.timelineFrames ? 0 : current + 1)
        : (current <= 0 ? this.timelineFrames : current - 1);
      this.progress = next / this.timelineFrames;
      this.timeScrub.value = String(this.progress);
      this.syncTimelineControls();
      this.applyPose(this.progress);
      this.syncPoseControlsToCurrentBone();
      this.setStatus(`${direction > 0 ? "Next" : "Previous"} frame: ${next}`);
    },

    currentFrame() {
      return Math.round(this.progress * this.timelineFrames);
    },

    syncTimelineControls() {
      const frame = this.currentFrame();
      const frameValue = String(frame);
      if (this.timelineScrub.value !== frameValue) {
        this.timelineScrub.value = frameValue;
      }
      const frameText = `${frame} / ${this.timelineFrames}`;
      if (this.frameReadout.textContent !== frameText) {
        this.frameReadout.textContent = frameText;
      }
      this.updateCurvePlayhead?.();
      this.syncAdaptiveConvertButton?.(frame);
    },

    syncPlaybackReadouts(options = {}) {
      const now = Number.isFinite(options.now) ? options.now : performance.now();
      const progressValue = String(this.progress);
      if (this.timeScrub?.value !== progressValue) {
        this.timeScrub.value = progressValue;
      }
      this.syncTimelineControls();
      if (options.poseControls === false) {
        return false;
      }
      if (options.throttlePoseControls && !options.force) {
        const interval = Number(this.playbackReadoutIntervalMs) || 180;
        if (now - (this.playbackReadoutLastUpdate || 0) < interval) {
          return false;
        }
      }
      this.playbackReadoutLastUpdate = now;
      if (options.poseControls !== false) {
        this.syncPoseControlsToCurrentBone();
      }
      return true;
    },

    updateTimelineKeyMarkers() {
      if (this.timelineKeys) {
        const frames = this.timelineKeyMarkerFrames();
        this.timelineKeys.replaceChildren(
          ...frames.map(({ frame, type }) => {
            const marker = document.createElement("button");
            marker.className = "timeline-key-marker";
            marker.classList.toggle("is-adaptive", type === "adaptive");
            marker.classList.toggle("is-authored", type === "authored");
            marker.classList.toggle("is-ik", type === "ik");
            marker.type = "button";
            marker.style.left = `${(frame / this.timelineFrames) * 100}%`;
            marker.title = type === "adaptive"
              ? `Frame ${frame} adaptive sync point`
              : type === "ik"
                ? `Frame ${frame} IK/chain animation key`
                : `Frame ${frame} edited animation key`;
            marker.addEventListener("click", (event) => {
              event.stopPropagation();
              this.hideTimelineKeyContextMenu?.();
              this.pausePlayback();
              this.discardUnkeyedPosePreview({ applyPose: false, syncControls: false });
              this.progress = frame / this.timelineFrames;
              this.timeScrub.value = String(this.progress);
              this.syncTimelineControls();
              this.syncPoseControls();
              this.applyPose(this.progress);
              this.syncAdaptiveConvertButton?.(frame);
            });
            marker.addEventListener("contextmenu", (event) => {
              event.preventDefault();
              event.stopPropagation();
              this.hideTimelineKeyContextMenu?.();
              if (type === "adaptive") {
                this.pausePlayback();
                this.progress = frame / this.timelineFrames;
                if (this.timeScrub) {
                  this.timeScrub.value = String(this.progress);
                }
                this.syncTimelineControls();
                this.syncPoseControls();
                this.applyPose(this.progress);
                this.syncAdaptiveConvertButton?.(frame);
              }
            });
            return marker;
          })
        );
      }
      this.syncAdaptiveConvertButton?.();
      this.updateBoneLayerList();
    },

    adaptiveTimelineMarkerAtFrame(frame = this.currentFrame()) {
      const targetFrame = Math.max(0, Math.min(this.timelineFrames, Math.round(Number(frame) || 0)));
      return this.timelineKeyMarkerFrames()
        .find((marker) => marker.type === "adaptive" && marker.frame === targetFrame) || null;
    },

    syncAdaptiveConvertButton(frame = this.currentFrame()) {
      if (!this.convertAdaptiveKeyButton) {
        return;
      }
      const marker = this.adaptiveTimelineMarkerAtFrame(frame);
      const canConvert = Boolean(marker);
      this.currentAdaptiveConvertFrame = canConvert ? marker.frame : null;
      this.convertAdaptiveKeyButton.hidden = !canConvert;
      this.convertAdaptiveKeyButton.disabled = !canConvert;
      this.convertAdaptiveKeyButton.setAttribute("aria-hidden", canConvert ? "false" : "true");
    },

    hideTimelineKeyContextMenu() {
      this.timelineKeyContextMenu?.remove?.();
      this.timelineKeyContextMenu = null;
    },

    convertAdaptiveMarkerToKeyframe(frame) {
      const boneName = this.poseBoneSelect?.value || this.activeBoneName || "";
      const targetNames = [...(this.timelineKeyMarkerBoneNames?.() || [])];
      const converted = this.convertAdaptiveGuideToKeyframe?.(
        frame,
        targetNames.length ? targetNames : [boneName],
        CURVE_CHANNEL_KEYS
      );
      if (!converted) {
        this.setStatus("No adaptive guide on that frame");
        return false;
      }
      this.progress = converted.frame / Math.max(1, this.timelineFrames);
      if (this.timeScrub) {
        this.timeScrub.value = String(this.progress);
      }
      this.syncTimelineControls();
      this.syncPatchJson();
      this.updateTimelineKeyMarkers();
      this.updateCounts();
      this.applyPose(this.progress);
      this.syncPoseControlsToCurrentBone();
      this.setStatus(
        `Converted frame ${converted.frame} adaptive guide to ${converted.bones === 1 ? "keyframe" : `${converted.bones} keyframes`}`
      );
      return true;
    },

    timelineKeyMarkerBoneNames() {
      const selected = this.poseBoneSelect?.value || this.activeBoneName || "";
      const names = new Set();
      if (selected && this.bones.has(selected)) {
        names.add(selected);
      }
      const mirrorName = this.mirrorMode && selected ? this.mirrorBoneName(selected) : "";
      if (mirrorName && this.bones.has(mirrorName)) {
        names.add(mirrorName);
      }
      return names;
    },

    timelineKeyMarkerFrames() {
      const names = this.timelineKeyMarkerBoneNames();
      if (!names.size) {
        return [];
      }
      const markers = new Map();
      const addMarkers = (keyframes, type) => {
        for (const [frame, framePose] of keyframes || []) {
          if ([...names].some((name) => framePose?.[name])) {
            const markerType = type === "authored"
              ? this.poseKeyframeMarkerType(frame, framePose)
              : type;
            markers.set(frame, { frame, type: markerType });
          }
        }
      };
      const showAdaptiveGuides = this.shouldUseAdaptiveEdits?.() === true;
      // Generated replacement keys are blue guide/sync points only while
      // Adaptive edits are active. Additive kinematics keeps those guides
      // hidden so it behaves like a broad FK/IK offset layer.
      if (showAdaptiveGuides) {
        addMarkers(this.adaptiveGuideKeyframes, "adaptive");
        addMarkers(this.adaptivePoseKeyframes, "adaptive");
      }
      if (!this.poseKeyframesGenerated || showAdaptiveGuides) {
        addMarkers(this.poseKeyframes, this.poseKeyframesGenerated ? "adaptive" : "authored");
      }
      return [...markers.values()].sort((a, b) => a.frame - b.frame);
    },

    selectPoseBone(name) {
      if (!this.bones.has(name)) {
        return;
      }
      this.setActiveBone(name);
    },

    boneLayerTrackBackground(frames = []) {
      const ticks = frames
        .map((frame) => THREE.MathUtils.clamp((frame / this.timelineFrames) * 100, 0, 100))
        .map((percent) => {
          const value = `${percent.toFixed(3)}%`;
          return `linear-gradient(90deg, transparent calc(${value} - 2px), rgba(223, 180, 93, 0.95) calc(${value} - 2px), rgba(223, 180, 93, 0.95) calc(${value} + 2px), transparent calc(${value} + 2px))`;
        });
      return ticks.join(", ");
    },

    updateBoneLayerList() {
      if (!this.boneLayerList) {
        return;
      }
      const scrollTop = this.boneLayerList.scrollTop;
      if (!this.boneLayerNames.length) {
        this.boneLayerList.replaceChildren();
        this.curveCanvas = null;
        this.curveContext = null;
        this.curvePlayhead = null;
        this.curveReadout = null;
        if (!this.app?.classList?.contains?.("is-timeline-compact")) {
          this.setTimelineCompact?.(true, { status: false });
        }
        this.updateRigBoneList();
        return;
      }
      const activeBone = this.poseBoneSelect.value;
      const mirroredActiveBone = this.mirrorMode ? this.mirrorBoneName(activeBone) : "";
      const timelineExpanded = !this.app?.classList?.contains?.("is-timeline-compact");
      const fallbackExpandedBone = timelineExpanded ? this.boneLayerNames[0] || "" : "";
      const expandedBone = this.expandedBoneName === null ? "" : this.expandedBoneName || activeBone || fallbackExpandedBone;
      this.curveCanvas = null;
      this.curveContext = null;
      this.curvePlayhead = null;
      this.curveReadout = null;
      const sortedKeyframes = [...this.poseKeyframes.entries()].sort(([a], [b]) => a - b);
      const rows = this.boneLayerNames.map((name) => {
        const mirrorName = this.mirrorMode ? this.mirrorBoneName(name) : "";
        const displayName = this.boneDisplayName(name);
        const isExpanded = name === expandedBone;
        const row = document.createElement("div");
        row.className = "bone-layer-row"
          + (name === activeBone ? " is-active" : "")
          + (name === mirroredActiveBone ? " is-mirror-active" : "")
          + (isExpanded ? " is-expanded" : "");

        const header = document.createElement("div");
        header.className = "bone-layer-header";

        const expandButton = document.createElement("button");
        expandButton.className = "bone-layer-expand-toggle";
        expandButton.type = "button";
        expandButton.textContent = isExpanded ? "-" : "+";
        expandButton.title = (isExpanded ? "Hide " : "Show ") + displayName + " curve";
        expandButton.setAttribute("aria-expanded", String(isExpanded));
        expandButton.addEventListener("click", () => {
          this.curveValueWindowLock = null;
          this.expandedBoneName = isExpanded ? null : name;
          if (this.expandedBoneName) {
            this.poseBoneSelect.value = name;
            this.syncPoseControls();
            this.updateTimelineKeyMarkers();
            return;
          }
          this.updateBoneLayerList();
        });

        const nameButton = document.createElement("button");
        nameButton.className = "bone-layer-name";
        nameButton.type = "button";
        nameButton.textContent = displayName;
        nameButton.title = this.mirrorMode && mirrorName ? displayName + " (" + name + " + " + mirrorName + ")" : name;
        nameButton.addEventListener("click", () => this.selectPoseBone(name));

        const values = document.createElement("div");
        values.className = "bone-layer-values";
        values.dataset.boneValues = name;
        values.replaceChildren(
          Object.assign(document.createElement("span"), { textContent: "X 0.00" }),
          Object.assign(document.createElement("span"), { textContent: "Y 0.00" }),
          Object.assign(document.createElement("span"), { textContent: "Z 0.00" })
        );

        const track = document.createElement("div");
        track.className = "bone-layer-track";
        track.setAttribute("aria-hidden", "true");
        const trackFrames = sortedKeyframes
          .filter(([, framePose]) => framePose[name] || (mirrorName && framePose[mirrorName]))
          .map(([frame]) => frame);
        if (trackFrames.length) {
          track.style.backgroundImage = this.boneLayerTrackBackground(trackFrames);
          track.title = `${displayName}: ${trackFrames.length} key ${trackFrames.length === 1 ? "frame" : "frames"}`;
        }

        if (isExpanded) {
          header.append(expandButton, nameButton, values, this.createCurveToolbar(name));
        } else {
          header.append(expandButton, nameButton, values, track);
        }
        row.append(header);
        if (isExpanded) {
          row.append(this.createCurvePanel(name));
        }
        return row;
      });
      this.boneLayerList.replaceChildren(...rows);
      this.boneLayerList.scrollTop = scrollTop;
      this.boneLayerValueNodes = [...this.boneLayerList.querySelectorAll("[data-bone-values]")];
      this.updateRigBoneList();
      this.updateBoneLayerValues({ force: true });
      if (timelineExpanded) {
        requestAnimationFrame(() => this.fitTimelineDrawerToContent?.({ persist: false }));
      }
      if (this.curveCanvas) {
        requestAnimationFrame(() => {
          this.drawCurveEditor?.();
          this.updateCurvePlayhead?.();
        });
      }
    },

    getBoneRelativePose(name) {
      const bone = this.bones.get(name);
      const rest = this.bindPose.find((entry) => entry.object === bone);
      if (!bone || !rest) {
        return { x: 0, y: 0, z: 0, px: 0, py: 0, pz: 0 };
      }
      const relativeQuaternion = rest.quaternion.clone().invert().multiply(bone.quaternion);
      const euler = new THREE.Euler().setFromQuaternion(relativeQuaternion, "XYZ");
      return {
        x: euler.x,
        y: euler.y,
        z: euler.z,
        px: bone.position.x - rest.position.x,
        py: bone.position.y - rest.position.y,
        pz: bone.position.z - rest.position.z
      };
    },

    timelineReadoutsVisible() {
      if (!this.app || !this.boneLayerList) {
        return false;
      }
      if (this.app.classList.contains("is-timeline-hidden") || this.app.classList.contains("is-timeline-compact")) {
        return false;
      }
      return this.boneLayerList.getClientRects().length > 0;
    },

    updateBoneLayerValues(options = {}) {
      if (!this.boneLayerList) {
        return;
      }
      if (!this.timelineReadoutsVisible()) {
        return;
      }
      const now = Number.isFinite(options.now) ? options.now : performance.now();
      if (options.throttle && !options.force) {
        const interval = Number(this.timelineReadoutIntervalMs) || 120;
        if (now - (this.timelineReadoutLastUpdate || 0) < interval) {
          return;
        }
      }
      this.timelineReadoutLastUpdate = now;
      let valueNodes = this.boneLayerValueNodes?.length
        ? this.boneLayerValueNodes
        : [...this.boneLayerList.querySelectorAll("[data-bone-values]")];
      this.boneLayerValueNodes = valueNodes;
      if (options.playback) {
        const activeNames = new Set([
          this.poseBoneSelect?.value,
          this.expandedBoneName
        ].filter(Boolean));
        valueNodes = valueNodes.filter((values) => activeNames.has(values.dataset.boneValues));
      }
      for (const values of valueNodes) {
        const pose = this.getBoneRelativePose(values.dataset.boneValues);
        const spans = values.querySelectorAll("span");
        const xText = `X ${pose.x.toFixed(2)}`;
        const yText = `Y ${pose.y.toFixed(2)}`;
        const zText = `Z ${pose.z.toFixed(2)}`;
        if (spans[0] && spans[0].textContent !== xText) spans[0].textContent = xText;
        if (spans[1] && spans[1].textContent !== yText) spans[1].textContent = yText;
        if (spans[2] && spans[2].textContent !== zText) spans[2].textContent = zText;
        const title = `Rotate X ${pose.x.toFixed(3)}, Y ${pose.y.toFixed(3)}, Z ${pose.z.toFixed(3)}; Move X ${pose.px.toFixed(3)}, Y ${pose.py.toFixed(3)}, Z ${pose.pz.toFixed(3)}`;
        if (values.title !== title) {
          values.title = title;
        }
      }
      if (options.force || !options.playback || now - (this.curveReadoutLastUpdate || 0) >= (Number(this.curveReadoutIntervalMs) || 600)) {
        this.curveReadoutLastUpdate = now;
        this.drawCurveEditor();
      }
    },

    getPoseAtFrame(frame, boneName) {
      return this.poseKeyframes.get(frame)?.[boneName] || null;
    },

    poseCurveKeyedPoints(boneName, channel, frames = [...this.poseKeyframes.keys()].sort((a, b) => a - b)) {
      const keyed = frames
        .filter((keyedFrame) => this.poseKeyframes.get(keyedFrame)?.[boneName]?.[channel] !== undefined)
        .map((keyedFrame) => ({
          frame: keyedFrame,
          value: finitePoseValue(this.poseKeyframes.get(keyedFrame)[boneName][channel])
        }));
      if (!keyed.length || !this.shouldUseImplicitAdditiveCurveEndpoints(boneName)) {
        return keyed;
      }
      const timelineFrames = Math.max(0, Math.round(Number(this.timelineFrames) || 0));
      const keyedFrames = new Map(keyed.map((point) => [point.frame, point]));
      if (!keyedFrames.has(0)) {
        keyedFrames.set(0, { frame: 0, value: 0, implicit: true });
      }
      if (timelineFrames > 0 && !keyedFrames.has(timelineFrames)) {
        keyedFrames.set(timelineFrames, { frame: timelineFrames, value: 0, implicit: true });
      }
      return [...keyedFrames.values()].sort((a, b) => a.frame - b.frame);
    },

    applyPoseLayer() {
      // Generated replacement keys are timeline guides for editing; the raw clip remains the live pose source.
      const generatedReplacementPreview = this.poseKeyframesGenerated && this.poseKeyframeMode === "replace";
      const interpolated = generatedReplacementPreview ? {} : this.interpolatedPoseForFrame(this.progress * this.timelineFrames);
      const rawSource = {
        ...interpolated,
        ...Object.fromEntries(this.manualPose)
      };
      const source = this.effectivePoseSource(rawSource);
      const replaceBaseClip = this.poseKeyframeMode === "replace" && this.poseKeyframes.size > 0 && !this.poseKeyframesGenerated;
      for (const [boneName, pose] of Object.entries(source)) {
        if (this.actorTarget?.mode === "bird-flap" || replaceBaseClip) {
          this.setBoneLayerPose(boneName, pose);
        } else {
          this.addBoneEuler(boneName, pose.x || 0, pose.y || 0, pose.z || 0);
          this.addBonePosition(boneName, pose.px || 0, pose.py || 0, pose.pz || 0);
        }
      }
    },

    interpolatedPoseForFrame(frame) {
      const frames = [...this.poseKeyframes.keys()].sort((a, b) => a - b);
      if (!frames.length) {
        return {};
      }
      const boneNames = new Set();
      for (const keyedFrame of frames) {
        Object.keys(this.poseKeyframes.get(keyedFrame) || {}).forEach((name) => boneNames.add(name));
      }

      const result = {};
      for (const boneName of boneNames) {
        result[boneName] = {};
        for (const channel of CURVE_CHANNEL_KEYS) {
          const keyed = this.poseCurveKeyedPoints(boneName, channel, frames);
          result[boneName][channel] = this.poseCurveValueForFrame(frame, boneName, channel, keyed);
        }
      }
      return result;
    },

    serializePoseKeyframes() {
      return this.serializePoseKeyframeMap(this.poseKeyframes);
    },

    serializePoseKeyframeKinds() {
      return [...(this.poseKeyframeKinds || new Map()).entries()]
        .filter(([frame, kind]) => Number.isInteger(frame) && kind === "ik")
        .map(([frame, kind]) => ({ frame, kind }))
        .sort((left, right) => left.frame - right.frame);
    },

    applySerializedPoseKeyframeKinds(serialized = []) {
      this.poseKeyframeKinds = new Map();
      for (const item of serialized || []) {
        if (Number.isInteger(item?.frame) && item.kind === "ik") {
          this.poseKeyframeKinds.set(item.frame, "ik");
        }
      }
    },

    serializePoseKeyframeMap(keyframes = this.poseKeyframes) {
      return [...(keyframes || new Map()).entries()]
        .sort(([a], [b]) => a - b)
        .map(([frame, bones]) => ({
          frame,
          bones: Object.fromEntries(
            Object.entries(bones || {}).map(([boneName, pose]) => [boneName, this.clonePose(pose)])
          )
        }));
    },

    applySerializedPoseKeyframes(serialized, options = {}) {
      this.poseKeyframes.clear();
      if (!options.preserveCurveHandles) {
        this.poseCurveHandles?.clear?.();
      }
      this.poseKeyframesGenerated = Boolean(options.generated);
      for (const key of serialized) {
        if (Number.isInteger(key.frame) && key.bones && typeof key.bones === "object") {
          this.poseKeyframes.set(key.frame, Object.fromEntries(
            Object.entries(key.bones).map(([boneName, pose]) => [boneName, this.clonePose(pose)])
          ));
        }
      }
      this.updateTimelineKeyMarkers();
    },

    serializedPoseKeyframeMap(serialized) {
      const result = new Map();
      for (const key of serialized || []) {
        if (Number.isInteger(key?.frame) && key.bones && typeof key.bones === "object") {
          result.set(key.frame, Object.fromEntries(
            Object.entries(key.bones).map(([boneName, pose]) => [boneName, this.clonePose(pose)])
          ));
        }
      }
      return result;
    },

    applyPoseKeyframeMap(keyframes) {
      this.poseKeyframes.clear();
      this.poseCurveHandles?.clear?.();
      this.poseKeyframesGenerated = false;
      for (const [frame, bones] of keyframes.entries()) {
        if (Number.isInteger(frame) && bones && typeof bones === "object") {
          this.poseKeyframes.set(frame, Object.fromEntries(
            Object.entries(bones).map(([boneName, pose]) => [boneName, this.clonePose(pose)])
          ));
        }
      }
      this.updateTimelineKeyMarkers();
    }
  });
}
