export function installTimelineAdditiveKinematicsMethods(BirdWeightEditor, deps) {
  const {
    THREE,
    CURVE_CHANNELS,
    CURVE_CHANNEL_KEYS,
    finitePoseValue
  } = deps;

  Object.assign(BirdWeightEditor.prototype, {
    shouldUseAdditiveKinematics() {
      return this.actorTarget?.mode !== "bird-flap"
        && this.additiveKinematicsToggle?.checked === true
        && Boolean(this.activeClipEntry || this.activeClipAction);
    },

    manualPoseAppliesAdditively() {
      if (this.shouldUseAdditiveKinematics?.()) {
        return this.actorTarget?.mode !== "bird-flap";
      }
      const replaceBaseClip = this.poseKeyframeMode === "replace"
        && this.poseKeyframes.size > 0
        && !this.poseKeyframesGenerated;
      return this.actorTarget?.mode !== "bird-flap" && !replaceBaseClip;
    },

    markManualPoseStorageMode(boneName, additive = this.manualPoseAppliesAdditively?.() === true) {
      this.manualPoseAdditiveNames ||= new Set();
      if (additive) {
        this.manualPoseAdditiveNames.add(boneName);
      } else {
        this.manualPoseAdditiveNames.delete(boneName);
      }
    },

    canUseAdditiveKinematicsForCurrentLayer() {
      return this.shouldUseAdditiveKinematics?.()
        && (
          this.poseKeyframeMode !== "replace"
          || this.poseKeyframes.size === 0
          || this.poseKeyframesGenerated
          || this.timelineKeysSourceWasAutoGenerated
        );
    },

    prepareAdditiveKinematicsLayerForEdit() {
      if (!this.canUseAdditiveKinematicsForCurrentLayer?.()) {
        return false;
      }
      const clearedSolvedLayer = this.poseKeyframeMode === "replace"
        && this.poseKeyframes.size > 0
        && (this.poseKeyframesGenerated || this.timelineKeysSourceWasAutoGenerated);
      if (clearedSolvedLayer) {
        this.preserveAdaptiveGuideMarkers?.(this.poseKeyframes);
        this.poseKeyframes.clear();
        this.poseCurveHandles?.clear?.();
      }
      this.poseKeyframeMode = "additive";
      this.poseKeyframesGenerated = false;
      this.timelineKeysSourceWasAutoGenerated = false;
      if (this.useTimelineKeysToggle) {
        this.useTimelineKeysToggle.checked = false;
      }
      if (this.adaptiveEditToggle) {
        this.adaptiveEditToggle.checked = false;
      }
      if (this.additiveKinematicsToggle) {
        this.additiveKinematicsToggle.checked = true;
      }
      this.syncTimelineSourceControl?.();
      return clearedSolvedLayer;
    },

    shouldUseImplicitAdditiveCurveEndpoints(boneName = "") {
      return this.actorTarget?.mode !== "bird-flap"
        && (this.poseKeyframeMode !== "replace" || Boolean(this.customBoneRecord?.(boneName)));
    },

    basePoseForFrame(frame, boneName) {
      const targetFrame = Math.max(0, Math.min(this.timelineFrames, Math.round(frame)));
      if (typeof this.sampleBasePoseForAutoKey === "function") {
        return this.sampleBasePoseForAutoKey(targetFrame / Math.max(1, this.timelineFrames), [boneName])?.[boneName]
          || { x: 0, y: 0, z: 0, px: 0, py: 0, pz: 0 };
      }
      return { x: 0, y: 0, z: 0, px: 0, py: 0, pz: 0 };
    },

    poseRotationQuaternion(pose = {}) {
      return new THREE.Quaternion().setFromEuler(new THREE.Euler(
        finitePoseValue(pose.x),
        finitePoseValue(pose.y),
        finitePoseValue(pose.z),
        "XYZ"
      ));
    },

    adaptivePoseFromAbsolutePose(frame, boneName, pose = {}) {
      const basePose = this.basePoseForFrame(frame, boneName);
      const result = {};
      if (["x", "y", "z"].some((channel) => pose[channel] !== undefined)) {
        const deltaEuler = new THREE.Euler().setFromQuaternion(
          this.poseRotationQuaternion(basePose).invert().multiply(this.poseRotationQuaternion(pose)),
          "XYZ"
        );
        result.x = finitePoseValue(deltaEuler.x);
        result.y = finitePoseValue(deltaEuler.y);
        result.z = finitePoseValue(deltaEuler.z);
      }
      for (const channel of ["px", "py", "pz"]) {
        if (pose[channel] !== undefined) {
          result[channel] = finitePoseValue(pose[channel] - finitePoseValue(basePose[channel]));
        }
      }
      return result;
    },

    adaptiveValueFromAbsoluteValue(frame, boneName, channel, value) {
      const basePose = this.basePoseForFrame(frame, boneName);
      return finitePoseValue(finitePoseValue(value) - finitePoseValue(basePose[channel]));
    },

    additiveKinematicsStatusText() {
      return "Additive kinematics: broad FK/IK edits layer over the animation";
    }
  });
}
