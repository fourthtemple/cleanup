const ROTATION_CHANNELS = new Set(["x", "y", "z"]);
const POSITION_CHANNELS = new Set(["px", "py", "pz"]);
const TWO_PI = Math.PI * 2;
const CONTACT_BONE_PATTERN = /(foot|toe|paw|ankle)/i;
const HIPS_BONE_PATTERN = /(hips|pelvis|root)/i;
const LEG_BONE_PATTERN = /(up\s*leg|upper\s*leg|thigh|leg|calf|shin|knee|foot|toe|ankle|paw)/i;
const FINGER_BONE_PATTERN = /(finger|thumb|index|middle|ring|pinky)/i;
const END_BONE_PATTERN = /(_end|end\b)/i;
const HAND_BONE_PATTERN = /hand/i;
const LOW_DETAIL_CORE_BONE_PATTERN = /(hips|pelvis|root|spine|neck|head|shoulder|arm|forearm|up\s*leg|upper\s*leg|thigh|leg|calf|shin|knee|foot|toe|ankle|paw)/i;

function channelWeight(channel, boneName = "") {
  if (ROTATION_CHANNELS.has(channel)) {
    return 1;
  }
  const isRoot = /hips|pelvis|root/i.test(boneName);
  return isRoot ? 0.35 : 0.18;
}

function addCandidate(candidates, frame, score, reason, maxFrame) {
  const targetFrame = Math.max(0, Math.min(maxFrame, Math.round(frame)));
  if (targetFrame <= 0 || targetFrame >= maxFrame || !Number.isFinite(score) || score <= 0) {
    return;
  }
  const current = candidates.get(targetFrame);
  if (!current || score > current.score) {
    candidates.set(targetFrame, { frame: targetFrame, score, reason });
  }
}

export function installAutoKeySolverMethods(BirdWeightEditor, deps) {
  const {
    THREE,
    CURVE_CHANNEL_KEYS,
    finitePoseValue
  } = deps;

  Object.assign(BirdWeightEditor.prototype, {
    solvedKeyDetailValue() {
      const value = Number(this.solvedKeyDetail?.value);
      return THREE.MathUtils.clamp(Number.isFinite(value) ? value : 0.2, 0, 1);
    },

    autoKeyDetailSettings(options = {}) {
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      const detailCurve = Math.pow(detail, 1.85);
      return {
        detail,
        maxKeys: Math.round(10 + detailCurve * 132),
        maxCurveKeys: Math.round(4 + detailCurve * 76)
      };
    },

    autoKeyStatusText(solution, label = solution?.label || "animation") {
      if (!solution) {
        return "";
      }
      const solverLabel = solution.solver === "walking" ? "walking solver, " : "";
      return `Auto-keyed ${label}: ${solverLabel}${solution.curveKeyCount} curve keys, ${solution.frames.length} frames, ${solution.boneNames.length} bones`;
    },

    async autoKeyActiveClip(options = {}) {
      if (!this.model) {
        if (!options.silent) {
          this.setStatus("Load a character first");
        }
        return null;
      }
      if (this.actorTarget?.mode !== "bird-flap") {
        const entry = this.activeClipEntry;
        if (!entry) {
          if (!options.silent) {
            this.setStatus("Choose an animation first");
          }
          return null;
        }
        if (!entry.clip) {
          await this.loadClipForEntry(entry);
        }
        if (!this.activeClipAction || this.activeClipEntry !== entry) {
          await this.playClipEntry(entry);
        }
      }

      this.stopSequencePreview?.({ applyPose: false, resetElapsed: true });
      this.pausePlayback?.();
      if (!options.silent) {
        this.setStatus("Solving animation keys...");
      }

      const solution = this.solveAutoKeyframesForActiveClip(options);
      if (!solution || solution.frames.length < 2 || !solution.boneNames.length) {
        if (!options.silent) {
          this.setStatus("No meaningful animation motion found");
        }
        return null;
      }

      if (options.pushUndo !== false) {
        this.pushUndoState?.("Auto key clip");
      }
      this.poseKeyframes.clear();
      this.poseCurveHandles?.clear?.();
      this.manualPose.clear();
      this.poseKeyframeMode = "replace";
      this.poseKeyframesGenerated = options.generated === true;
      this.timelineKeysSourceWasAutoGenerated = false;

      for (const curve of solution.curves) {
        for (const key of curve.keys) {
          const framePose = this.poseKeyframes.get(key.frame) || {};
          const bonePose = framePose[curve.boneName] || {};
          bonePose[curve.channel] = finitePoseValue(key.value);
          framePose[curve.boneName] = bonePose;
          this.poseKeyframes.set(key.frame, framePose);
          this.setCurveHandleFor?.(curve.boneName, curve.channel, key.frame, key);
        }
      }

      this.progress = 0;
      if (this.timeScrub) {
        this.timeScrub.value = "0";
      }
      if (this.timelineScrub) {
        this.timelineScrub.value = "0";
      }
      if (options.selectPrimaryBone !== false && solution.primaryBoneName && this.bones.has(solution.primaryBoneName)) {
        this.setActiveBone(solution.primaryBoneName);
        this.expandedBoneName = solution.primaryBoneName;
      }
      this.applyPose(0);
      this.syncPoseControlsToCurrentBone();
      this.syncTimelineControls();
      this.updateTimelineKeyMarkers();
      this.syncPatchJson();
      this.updateCounts();
      const label = this.activeClipEntry?.name || this.activeClipEntry?.id || this.actorTarget?.label || "animation";
      solution.label = label;
      if (!options.silent) {
        this.setStatus(this.autoKeyStatusText(solution, label));
      }
      return solution;
    },

    async autoKeyClipOnLoadIfNeeded(options = {}) {
      if (
        this.actorTarget?.mode === "bird-flap"
        || !this.activeClipEntry
        || this.poseKeyframes.size
        || this.manualPose.size
      ) {
        return null;
      }
      const settings = this.autoKeyDetailSettings(options);
      const solution = await this.autoKeyActiveClip({
        ...settings,
        maxKeys: options.maxKeys || settings.maxKeys,
        maxCurveKeys: options.maxCurveKeys || settings.maxCurveKeys,
        minKeys: options.minKeys || 6,
        pushUndo: false,
        generated: true,
        selectPrimaryBone: false,
        silent: true
      });
      if (solution && !options.silent) {
        this.setStatus(this.autoKeyStatusText(solution, solution.label));
      }
      if (solution && this.shouldUseTimelineKeysAsSource?.()) {
        this.useTimelineGuideKeysAsSource?.({ silent: true });
      } else {
        this.syncTimelineSourceControl?.();
      }
      return solution;
    },

    async rebuildAutoKeyedTimelineFromDetail(options = {}) {
      if (!this.model || !this.activeClipEntry) {
        this.updateRangeOutputs?.();
        return null;
      }
      const canRebuild = !this.poseKeyframes.size
        || this.hasGeneratedTimelineGuideKeys?.()
        || (this.timelineKeysAreActiveSource?.() && this.timelineKeysSourceWasAutoGenerated);
      if (!canRebuild) {
        this.updateRangeOutputs?.();
        this.setStatus("Key detail applies before authored timeline edits");
        return null;
      }
      const useAsSource = this.shouldUseTimelineKeysAsSource?.() !== false;
      if (options.pushUndo) {
        this.pushUndoState?.("Change key detail", { clearManualPose: true });
      }
      this.poseKeyframes.clear();
      this.poseCurveHandles?.clear?.();
      this.manualPose.clear();
      this.poseKeyframeMode = "additive";
      this.poseKeyframesGenerated = false;
      this.timelineKeysSourceWasAutoGenerated = false;
      const settings = this.autoKeyDetailSettings(options);
      const solution = await this.autoKeyActiveClip({
        ...settings,
        minKeys: options.minKeys || 6,
        pushUndo: false,
        generated: true,
        silent: true
      });
      if (solution && useAsSource) {
        this.useTimelineGuideKeysAsSource?.({ silent: true });
      } else {
        this.syncTimelineSourceControl?.();
      }
      this.updateRangeOutputs?.();
      if (solution) {
        const solverLabel = solution.solver === "walking" ? " walking" : "";
        this.setStatus(`Solved ${Math.round(settings.detail * 100)}%${solverLabel} detail: ${solution.curveKeyCount} curve keys`);
      }
      return solution;
    },

    solveAutoKeyframesForActiveClip(options = {}) {
      const maxFrame = Math.max(4, Math.floor(Number(options.timelineFrames) || this.timelineFrames || 96));
      const detailSettings = this.autoKeyDetailSettings(options);
      const maxKeys = Math.max(4, Math.min(maxFrame + 1, Math.floor(Number(options.maxKeys) || detailSettings.maxKeys)));
      const maxCurveKeys = Math.max(4, Math.min(maxFrame + 1, Math.floor(Number(options.maxCurveKeys) || detailSettings.maxCurveKeys || Math.ceil(maxKeys * 0.25))));
      const allBoneNames = this.autoKeyCandidateBoneNames();
      if (!allBoneNames.length) {
        return null;
      }

      const samples = [];
      for (let frame = 0; frame <= maxFrame; frame += 1) {
        samples.push(this.sampleBasePoseForAutoKey(frame / maxFrame, allBoneNames));
      }
      this.unwrapAutoKeyRotationSamples(samples, allBoneNames);

      const walking = this.autoKeyWalkingContext(samples, allBoneNames, {
        maxFrame,
        detail: detailSettings.detail
      });
      this.autoKeyApplyWalkingFootLocks(samples, walking, {
        detail: detailSettings.detail
      });
      const moving = this.autoKeyMovingBoneInfo(samples, allBoneNames, {
        walking,
        detail: detailSettings.detail
      });
      const boneNames = moving.bones.map((item) => item.name);
      if (!boneNames.length) {
        return null;
      }

      const curves = this.autoKeySolveChannelCurves(samples, moving.bones, {
        maxFrame,
        maxCurveKeys,
        detail: detailSettings.detail,
        walking
      });
      if (!curves.length) {
        return null;
      }
      const frames = [...new Set(curves.flatMap((curve) => curve.keys.map((key) => key.frame)))].sort((a, b) => a - b);
      const keyedBoneNames = [...new Set(curves.map((curve) => curve.boneName))]
        .sort((a, b) => {
          const aScore = moving.bones.find((bone) => bone.name === a)?.score || 0;
          const bScore = moving.bones.find((bone) => bone.name === b)?.score || 0;
          return bScore - aScore;
        });

      return {
        frames,
        samples,
        curves,
        curveKeyCount: curves.reduce((sum, curve) => sum + curve.keys.length, 0),
        solver: walking?.active ? "walking" : "curve",
        contactFrameCount: walking?.active ? walking.denseFrames.size : 0,
        boneNames: keyedBoneNames,
        primaryBoneName: moving.bones.find((bone) => keyedBoneNames.includes(bone.name))?.name || keyedBoneNames[0] || ""
      };
    },

    autoKeyCandidateBoneNames() {
      const names = this.boneLayerNames?.length ? this.boneLayerNames : [...this.bones.keys()];
      return [...new Set(names)].filter((name) => this.bones.has(name));
    },

    autoKeyBoneSearchText(name) {
      return `${name || ""} ${this.boneDisplayName?.(name) || ""}`.toLowerCase();
    },

    autoKeyFindBoneName(boneNames, patterns = [], excludes = []) {
      return boneNames.find((name) => {
        const search = this.autoKeyBoneSearchText(name);
        return patterns.some((pattern) => pattern.test(search))
          && !excludes.some((pattern) => pattern.test(search));
      }) || "";
    },

    autoKeyFindSideBoneName(boneNames, side, patterns = [], excludes = []) {
      const sidePattern = side === "left" ? /left/i : /right/i;
      return this.autoKeyFindBoneName(
        boneNames.filter((name) => sidePattern.test(this.autoKeyBoneSearchText(name))),
        patterns,
        excludes
      );
    },

    autoKeyWalkingRigInfo(boneNames) {
      const uniqueNames = [...new Set(boneNames)].filter((name) => this.bones.has(name));
      const hipsName = this.autoKeyFindBoneName(uniqueNames, [/^hips$/i, /\bhips\b/i])
        || this.autoKeyFindBoneName(uniqueNames, [HIPS_BONE_PATTERN]);
      const sides = ["left", "right"].map((side) => {
        const upperName = this.autoKeyFindSideBoneName(uniqueNames, side, [/up\s*leg|upper\s*leg|thigh/i]);
        const lowerName = this.autoKeyFindSideBoneName(
          uniqueNames,
          side,
          [/\bleg\b|calf|shin|knee/i],
          [/up\s*leg|upper\s*leg|thigh|foot|toe|ankle|paw/i]
        );
        const footName = this.autoKeyFindSideBoneName(uniqueNames, side, [/foot|ankle|paw/i], [/toe/i]);
        const toeName = this.autoKeyFindSideBoneName(uniqueNames, side, [/toe|ball/i]);
        return {
          side,
          upperName,
          lowerName,
          footName,
          toeName,
          contactName: toeName || footName
        };
      });
      const managedBoneNames = new Set([hipsName].filter(Boolean));
      const contactBoneNames = new Set();
      for (const side of sides) {
        for (const name of [side.upperName, side.lowerName, side.footName, side.toeName]) {
          if (name) {
            managedBoneNames.add(name);
          }
        }
        for (const name of [side.footName, side.toeName]) {
          if (name) {
            contactBoneNames.add(name);
          }
        }
      }
      for (const name of uniqueNames) {
        const search = this.autoKeyBoneSearchText(name);
        if ((/left|right/i.test(search) && LEG_BONE_PATTERN.test(search)) || CONTACT_BONE_PATTERN.test(search)) {
          managedBoneNames.add(name);
        }
      }
      return {
        active: Boolean(hipsName && contactBoneNames.size),
        hipsName,
        sides,
        managedBoneNames,
        contactBoneNames
      };
    },

    autoKeyWalkingContext(samples, boneNames, options = {}) {
      const maxFrame = Math.max(1, Math.floor(Number(options.maxFrame) || samples.length - 1));
      const detail = THREE.MathUtils.clamp(Number(options.detail) || 0, 0, 1);
      const rig = this.autoKeyWalkingRigInfo(boneNames);
      if (!rig.active) {
        return { active: false };
      }
      const sideSignals = this.sampleWalkingContactSignals(maxFrame, rig);
      const transitionFrames = new Set([0, maxFrame]);
      const denseFrames = new Set();
      const contactHoldFrames = new Set();
      const balanceFrames = new Set();
      for (const side of sideSignals) {
        const contact = this.autoKeyAnalyzeFootContact(side.frames, { maxFrame, detail });
        side.contact = contact;
        for (const frame of contact.transitionFrames) {
          transitionFrames.add(frame);
        }
        for (const frame of contact.denseFrames) {
          denseFrames.add(frame);
        }
        for (const frame of contact.holdFrames) {
          contactHoldFrames.add(frame);
        }
        for (const frame of contact.balanceFrames) {
          balanceFrames.add(frame);
        }
      }
      const footLock = this.autoKeyBuildFootLockCorrections(sideSignals, { maxFrame, detail });
      return {
        ...rig,
        active: denseFrames.size > 0 || transitionFrames.size > 2 || footLock.active,
        detail,
        transitionFrames,
        denseFrames,
        contactHoldFrames,
        balanceFrames,
        footLock,
        sideSignals
      };
    },

    sampleWalkingContactSignals(maxFrame, rig) {
      const previousKeyframes = this.poseKeyframes;
      const previousManualPose = this.manualPose;
      const previousMode = this.poseKeyframeMode;
      const previousSampleTime = this.lastClipSampleTime;
      const sides = rig.sides
        .filter((side) => side.footName || side.toeName)
        .map((side) => ({ ...side, frames: [] }));
      try {
        this.poseKeyframes = new Map();
        this.manualPose = new Map();
        this.poseKeyframeMode = "additive";
        for (let frame = 0; frame <= maxFrame; frame += 1) {
          this.applyPose(frame / maxFrame);
          this.model.updateMatrixWorld(true);
          const hipsPosition = new THREE.Vector3();
          const hipsBone = rig.hipsName ? this.bones.get(rig.hipsName) : null;
          const hipsParent = hipsBone?.parent || null;
          if (hipsBone) {
            hipsBone.getWorldPosition(hipsPosition);
          }
          for (const side of sides) {
            const sampledPoints = [side.footName, side.toeName]
              .filter(Boolean)
              .map((name) => {
                const bone = this.bones.get(name);
                if (!bone) {
                  return null;
                }
                const position = new THREE.Vector3();
                bone.getWorldPosition(position);
                return { name, position };
              })
              .filter(Boolean);
            if (!sampledPoints.length) {
              side.frames[frame] = null;
              continue;
            }
            const contactSample = sampledPoints.find((sample) => sample.name === side.contactName)
              || sampledPoints.find((sample) => sample.name === side.toeName)
              || sampledPoints.reduce((lowest, current) => (current.position.y < lowest.position.y ? current : lowest), sampledPoints[0]);
            const point = contactSample.position;
            const localPoint = point.clone();
            if (hipsParent) {
              hipsParent.worldToLocal(localPoint);
            }
            side.frames[frame] = {
              contactName: contactSample.name,
              x: point.x,
              y: point.y,
              z: point.z,
              localX: localPoint.x,
              localY: localPoint.y,
              localZ: localPoint.z,
              hipX: hipsBone ? hipsPosition.x : null,
              hipY: hipsBone ? hipsPosition.y : null,
              hipZ: hipsBone ? hipsPosition.z : null
            };
          }
        }
      } finally {
        this.poseKeyframes = previousKeyframes;
        this.manualPose = previousManualPose;
        this.poseKeyframeMode = previousMode;
        this.lastClipSampleTime = previousSampleTime;
      }
      return sides;
    },

    autoKeyAnalyzeFootContact(frames, options = {}) {
      const maxFrame = Math.max(1, Math.floor(Number(options.maxFrame) || frames.length - 1));
      const detail = THREE.MathUtils.clamp(Number(options.detail) || 0, 0, 1);
      const validFrames = frames
        .map((point, frame) => (point ? { ...point, frame } : null))
        .filter(Boolean);
      const transitionFrames = new Set();
      const denseFrames = new Set();
      const holdFrames = new Set();
      const balanceFrames = new Set();
      if (validFrames.length < 3) {
        return {
          transitionFrames,
          denseFrames,
          holdFrames,
          balanceFrames,
          windows: []
        };
      }

      const minY = Math.min(...validFrames.map((point) => point.y));
      const maxY = Math.max(...validFrames.map((point) => point.y));
      const yRange = Math.max(0, maxY - minY);
      const speeds = frames.map((point, frame) => {
        const previous = frame > 0 ? frames[frame - 1] : null;
        if (!point || !previous) {
          return 0;
        }
        const dx = point.x - previous.x;
        const dz = point.z - previous.z;
        return Math.sqrt(dx * dx + dz * dz);
      });
      const verticalSpeeds = frames.map((point, frame) => {
        const previous = frame > 0 ? frames[frame - 1] : null;
        return point && previous ? Math.abs(point.y - previous.y) : 0;
      });
      const finiteSpeeds = speeds.slice(1).filter((speed) => Number.isFinite(speed));
      const maxSpeed = Math.max(0, ...finiteSpeeds);
      const averageSpeed = finiteSpeeds.reduce((sum, speed) => sum + speed, 0) / Math.max(1, finiteSpeeds.length);
      const contactHeight = minY + Math.max(0.0015, yRange * 0.24);
      const firmContactHeight = minY + Math.max(0.001, yRange * 0.1);
      const speedThreshold = Math.max(0.0004, averageSpeed * 0.8, maxSpeed * 0.16);
      const verticalThreshold = Math.max(0.0004, yRange * 0.035);
      let contact = frames.map((point, frame) => Boolean(
        point
        && point.y <= contactHeight
        && (
          speeds[frame] <= speedThreshold
          || verticalSpeeds[frame] <= verticalThreshold
          || point.y <= firmContactHeight
        )
      ));
      const contactCount = contact.filter(Boolean).length;
      if (contactCount < Math.max(3, Math.round(maxFrame * 0.08))) {
        const sortedByY = [...validFrames].sort((a, b) => a.y - b.y);
        const fallbackIndex = Math.min(sortedByY.length - 1, Math.max(0, Math.round(sortedByY.length * 0.28)));
        const fallbackHeight = sortedByY[fallbackIndex]?.y ?? contactHeight;
        contact = frames.map((point) => Boolean(point && point.y <= fallbackHeight));
      }

      const windows = [];
      let startFrame = null;
      for (let frame = 0; frame <= maxFrame; frame += 1) {
        if (contact[frame] && startFrame === null) {
          startFrame = frame;
        }
        if ((!contact[frame] || frame === maxFrame) && startFrame !== null) {
          const endFrame = contact[frame] && frame === maxFrame ? frame : frame - 1;
          if (endFrame - startFrame >= 1) {
            windows.push({ start: startFrame, end: endFrame });
          }
          startFrame = null;
        }
      }

      const margin = detail >= 0.9 ? 2 : detail >= 0.55 ? 1 : 0;
      const stride = detail >= 0.95
        ? 1
        : detail >= 0.75 ? 2
          : detail >= 0.55 ? 3
            : detail >= 0.35 ? 4
              : detail >= 0.18 ? 5
                : 7;
      const transitionRadius = detail >= 0.45 ? 1 : 0;
      for (const window of windows) {
        const mid = Math.round((window.start + window.end) * 0.5);
        for (const frame of [window.start, mid, window.end]) {
          holdFrames.add(frame);
          for (let offset = -transitionRadius; offset <= transitionRadius; offset += 1) {
            transitionFrames.add(Math.max(0, Math.min(maxFrame, frame + offset)));
          }
        }
        const lowPoint = validFrames
          .filter((point) => point.frame >= window.start && point.frame <= window.end)
          .sort((a, b) => a.y - b.y)[0];
        if (lowPoint) {
          transitionFrames.add(lowPoint.frame);
          holdFrames.add(lowPoint.frame);
        }
        this.autoKeyAddSupportBalanceFrames(
          validFrames.filter((point) => point.frame >= window.start && point.frame <= window.end),
          balanceFrames,
          { detail, maxFrame }
        );
        for (let frame = Math.max(0, window.start - margin); frame <= Math.min(maxFrame, window.end + margin); frame += stride) {
          denseFrames.add(frame);
        }
      }

      for (const frame of balanceFrames) {
        transitionFrames.add(frame);
        holdFrames.add(frame);
      }
      const apex = [...validFrames].sort((a, b) => b.y - a.y)[0];
      if (apex) {
        transitionFrames.add(apex.frame);
      }
      return {
        transitionFrames,
        denseFrames,
        holdFrames,
        balanceFrames,
        windows
      };
    },

    autoKeyBuildFootLockCorrections(sideSignals, options = {}) {
      const detail = THREE.MathUtils.clamp(Number(options.detail) || 0, 0, 1);
      const maxFrame = Math.max(1, Math.floor(Number(options.maxFrame) || this.timelineFrames || 96));
      const strength = this.autoKeyFootLockStrength(detail);
      const corrections = new Map();
      if (strength <= 0.001) {
        return { active: false, strength: 0, corrections };
      }

      for (const side of sideSignals || []) {
        const contact = side.contact;
        if (!contact?.windows?.length) {
          continue;
        }
        for (const window of contact.windows) {
          const points = [];
          for (let frame = window.start; frame <= window.end; frame += 1) {
            const point = side.frames?.[frame];
            if (
              point
              && Number.isFinite(point.localX)
              && Number.isFinite(point.localZ)
            ) {
              points.push({ ...point, frame });
            }
          }
          if (points.length < 2) {
            continue;
          }
          const anchor = this.autoKeyFootLockAnchorPoint(points);
          if (!anchor) {
            continue;
          }
          for (const point of points) {
            const dx = finitePoseValue(anchor.localX - point.localX);
            const dz = finitePoseValue(anchor.localZ - point.localZ);
            const distance = Math.hypot(dx, dz);
            if (distance <= this.autoKeyFootLockDriftThreshold(detail)) {
              continue;
            }
            const weight = this.autoKeyFootLockWindowWeight(point.frame, window, detail);
            if (weight <= 0) {
              continue;
            }
            const clamped = this.autoKeyClampFootLockCorrection(dx, dz, detail);
            this.autoKeyAccumulateFootLockCorrection(corrections, point.frame, clamped.x, clamped.z, weight);
          }
        }
      }

      const smoothedCorrections = this.autoKeySmoothFootLockCorrections(corrections, maxFrame, detail);
      return {
        active: smoothedCorrections.size > 0,
        strength,
        corrections: smoothedCorrections
      };
    },

    autoKeyFootLockStrength(detail = this.solvedKeyDetailValue()) {
      const normalizedDetail = THREE.MathUtils.clamp(Number(detail) || 0, 0, 1);
      if (normalizedDetail >= 0.98) {
        return 0;
      }
      return Math.pow(1 - normalizedDetail, 1.18);
    },

    autoKeyFootLockDriftThreshold(detail = this.solvedKeyDetailValue()) {
      const normalizedDetail = THREE.MathUtils.clamp(Number(detail) || 0, 0, 1);
      return THREE.MathUtils.lerp(0.0006, 0.004, normalizedDetail);
    },

    autoKeyFootLockAnchorPoint(points) {
      if (!points?.length) {
        return null;
      }
      const minY = Math.min(...points.map((point) => finitePoseValue(point.y)));
      const maxY = Math.max(...points.map((point) => finitePoseValue(point.y)));
      const firmHeight = minY + Math.max(0.001, (maxY - minY) * 0.18);
      return points.find((point) => finitePoseValue(point.y) <= firmHeight) || points[0];
    },

    autoKeyFootLockWindowWeight(frame, window, detail = this.solvedKeyDetailValue()) {
      const length = Math.max(1, window.end - window.start);
      if (length <= 2) {
        return 1;
      }
      const normalizedDetail = THREE.MathUtils.clamp(Number(detail) || 0, 0, 1);
      const edgePortion = THREE.MathUtils.lerp(0.32, 0.16, normalizedDetail);
      const t = THREE.MathUtils.clamp((frame - window.start) / length, 0, 1);
      const fadeIn = this.autoKeySmoothStep(0, edgePortion, t);
      const fadeOut = this.autoKeySmoothStep(0, edgePortion, 1 - t);
      return fadeIn * fadeOut;
    },

    autoKeySmoothStep(edge0, edge1, value) {
      const span = edge1 - edge0;
      if (Math.abs(span) < 0.000001) {
        return value >= edge1 ? 1 : 0;
      }
      const t = THREE.MathUtils.clamp((value - edge0) / span, 0, 1);
      return t * t * (3 - 2 * t);
    },

    autoKeyClampFootLockCorrection(x, z, detail = this.solvedKeyDetailValue()) {
      const normalizedDetail = THREE.MathUtils.clamp(Number(detail) || 0, 0, 1);
      const maxDistance = THREE.MathUtils.lerp(18, 6, normalizedDetail);
      const distance = Math.hypot(x, z);
      if (!Number.isFinite(distance) || distance <= maxDistance) {
        return { x, z };
      }
      const scale = maxDistance / Math.max(distance, 0.000001);
      return { x: x * scale, z: z * scale };
    },

    autoKeyAccumulateFootLockCorrection(corrections, frame, x, z, weight = 1) {
      if (!Number.isFinite(x) || !Number.isFinite(z) || !Number.isFinite(weight) || weight <= 0) {
        return;
      }
      const targetFrame = Math.round(frame);
      if (!Number.isFinite(targetFrame)) {
        return;
      }
      const current = corrections.get(targetFrame) || { x: 0, z: 0, weight: 0 };
      current.x += x * weight;
      current.z += z * weight;
      current.weight += weight;
      corrections.set(targetFrame, current);
    },

    autoKeySmoothFootLockCorrections(corrections, maxFrame, detail = this.solvedKeyDetailValue()) {
      if (!corrections?.size) {
        return new Map();
      }
      const normalizedDetail = THREE.MathUtils.clamp(Number(detail) || 0, 0, 1);
      const radius = Math.round(THREE.MathUtils.lerp(5, 1, normalizedDetail));
      const result = new Map();
      for (let frame = 0; frame <= maxFrame; frame += 1) {
        let x = 0;
        let z = 0;
        let totalKernelWeight = 0;
        for (let offset = -radius; offset <= radius; offset += 1) {
          const sourceFrame = frame + offset;
          const distanceT = Math.abs(offset) / Math.max(1, radius + 1);
          const smoothWeight = 1 - distanceT;
          totalKernelWeight += smoothWeight;
          const item = corrections.get(sourceFrame);
          if (item?.weight) {
            x += (item.x / item.weight) * smoothWeight;
            z += (item.z / item.weight) * smoothWeight;
          }
        }
        if (totalKernelWeight <= 0) {
          continue;
        }
        const smoothedX = x / totalKernelWeight;
        const smoothedZ = z / totalKernelWeight;
        if (Math.hypot(smoothedX, smoothedZ) <= this.autoKeyFootLockDriftThreshold(normalizedDetail) * 0.2) {
          continue;
        }
        result.set(frame, { x: smoothedX, z: smoothedZ, weight: 1 });
      }
      return result;
    },

    autoKeyApplyWalkingFootLocks(samples, walking, options = {}) {
      const footLock = walking?.footLock;
      const hipsName = walking?.hipsName;
      if (!footLock?.active || !hipsName || !footLock.corrections?.size) {
        return;
      }
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      const strength = Number.isFinite(footLock.strength)
        ? footLock.strength
        : this.autoKeyFootLockStrength(detail);
      for (const [frame, correction] of footLock.corrections.entries()) {
        const pose = samples[frame]?.[hipsName];
        if (!pose || !correction.weight) {
          continue;
        }
        const x = (correction.x / correction.weight) * strength;
        const z = (correction.z / correction.weight) * strength;
        pose.px = finitePoseValue((pose.px || 0) + x);
        pose.pz = finitePoseValue((pose.pz || 0) + z);
      }
    },

    autoKeyAddSupportBalanceFrames(points, targetFrames, options = {}) {
      const detail = THREE.MathUtils.clamp(Number(options.detail) || 0, 0, 1);
      const maxFrame = Math.max(1, Math.floor(Number(options.maxFrame) || this.timelineFrames || 96));
      const maxPerWindow = detail < 0.15 ? 2 : detail < 0.55 ? 3 : 4;
      const candidates = new Map();
      const metrics = [
        { weight: 1, value: (point) => point.hipX - point.x },
        { weight: 0.75, value: (point) => point.hipZ - point.z },
        { weight: 0.45, value: (point) => point.hipY - point.y }
      ];

      for (const metric of metrics) {
        const measured = points
          .map((point) => ({
            frame: point.frame,
            value: metric.value(point)
          }))
          .filter((point) => Number.isFinite(point.value));
        if (measured.length < 3) {
          continue;
        }
        const min = measured.reduce((best, point) => (point.value < best.value ? point : best), measured[0]);
        const max = measured.reduce((best, point) => (point.value > best.value ? point : best), measured[0]);
        const range = max.value - min.value;
        if (range < 0.0015) {
          continue;
        }
        for (const point of [min, max]) {
          if (point.frame <= 0 || point.frame >= maxFrame) {
            continue;
          }
          const score = range * metric.weight;
          const current = candidates.get(point.frame);
          if (!current || score > current.score) {
            candidates.set(point.frame, { frame: point.frame, score });
          }
        }
      }

      for (const candidate of [...candidates.values()].sort((a, b) => b.score - a.score).slice(0, maxPerWindow)) {
        targetFrames.add(candidate.frame);
      }
    },

    sampleBasePoseForAutoKey(progress, boneNames) {
      const previousKeyframes = this.poseKeyframes;
      const previousManualPose = this.manualPose;
      const previousMode = this.poseKeyframeMode;
      const previousSampleTime = this.lastClipSampleTime;
      try {
        this.poseKeyframes = new Map();
        this.manualPose = new Map();
        this.poseKeyframeMode = "additive";
        this.applyPose(THREE.MathUtils.clamp(progress, 0, 1));
        this.model.updateMatrixWorld(true);
        const sample = {};
        for (const boneName of boneNames) {
          sample[boneName] = this.clonePose(this.getBoneRelativePose(boneName));
        }
        return sample;
      } finally {
        this.poseKeyframes = previousKeyframes;
        this.manualPose = previousManualPose;
        this.poseKeyframeMode = previousMode;
        this.lastClipSampleTime = previousSampleTime;
      }
    },

    unwrapAutoKeyRotationSamples(samples, boneNames) {
      for (const boneName of boneNames) {
        for (const channel of CURVE_CHANNEL_KEYS.filter((key) => ROTATION_CHANNELS.has(key))) {
          let previous = finitePoseValue(samples[0]?.[boneName]?.[channel]);
          for (let index = 1; index < samples.length; index += 1) {
            let value = finitePoseValue(samples[index]?.[boneName]?.[channel]);
            while (value - previous > Math.PI) value -= TWO_PI;
            while (value - previous < -Math.PI) value += TWO_PI;
            samples[index][boneName][channel] = finitePoseValue(value);
            previous = value;
          }
        }
      }
    },

    autoKeyMovingBoneInfo(samples, boneNames, options = {}) {
      const bones = [];
      const walking = options.walking?.active ? options.walking : null;
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      for (const boneName of boneNames) {
        if (!this.autoKeyShouldIncludeBoneForDetail(boneName, detail, walking)) {
          continue;
        }
        let score = 0;
        let offsetScore = 0;
        for (const channel of CURVE_CHANNEL_KEYS) {
          const values = samples.map((sample) => finitePoseValue(sample?.[boneName]?.[channel]));
          const min = Math.min(...values);
          const max = Math.max(...values);
          const range = max - min;
          const weight = channelWeight(channel, boneName);
          score += range * weight;
          offsetScore += Math.max(Math.abs(min), Math.abs(max)) * weight;
        }
        const isWalkingManaged = walking?.managedBoneNames?.has(boneName);
        const movementThreshold = isWalkingManaged ? 0.0015 : 0.004;
        const offsetThreshold = isWalkingManaged ? 0.002 : 0.006;
        if (score > movementThreshold || offsetScore > offsetThreshold) {
          bones.push({ name: boneName, score: score + offsetScore * 0.25 });
        }
      }
      bones.sort((a, b) => b.score - a.score);
      return { bones };
    },

    autoKeyShouldIncludeBoneForDetail(boneName, detail = this.solvedKeyDetailValue(), walking = null) {
      const normalizedDetail = THREE.MathUtils.clamp(Number(detail) || 0, 0, 1);
      const search = this.autoKeyBoneSearchText(boneName);
      if (walking?.managedBoneNames?.has(boneName)) {
        return true;
      }
      if (normalizedDetail >= 0.65) {
        return true;
      }
      if (END_BONE_PATTERN.test(search)) {
        return normalizedDetail >= 0.75;
      }
      if (FINGER_BONE_PATTERN.test(search)) {
        return normalizedDetail >= 0.35;
      }
      if (HAND_BONE_PATTERN.test(search) && !/forearm|shoulder|upper\s*arm|lower\s*arm/i.test(search)) {
        return normalizedDetail >= 0.12;
      }
      if (LOW_DETAIL_CORE_BONE_PATTERN.test(search)) {
        return true;
      }
      return normalizedDetail >= 0.25;
    },

    autoKeySolveChannelCurves(samples, bones, options = {}) {
      const maxFrame = Math.max(1, Math.floor(Number(options.maxFrame) || samples.length - 1));
      const maxCurveKeys = Math.max(4, Math.floor(Number(options.maxCurveKeys) || 12));
      const curves = [];

      for (const bone of bones) {
        for (const channel of CURVE_CHANNEL_KEYS) {
          const values = samples.map((sample) => finitePoseValue(sample?.[bone.name]?.[channel]));
          const info = this.autoKeyChannelInfo(values, channel, bone.name, {
            walking: options.walking,
            detail: options.detail
          });
          if (!info.include) {
            continue;
          }
          const frames = this.autoKeySelectCurveFrames(values, {
            channel,
            boneName: bone.name,
            maxFrame,
            maxCurveKeys,
            info,
            detail: options.detail,
            walking: options.walking
          });
          if (frames.length < 2) {
            continue;
          }
          curves.push({
            boneName: bone.name,
            channel,
            range: info.range,
            keys: this.autoKeyCurveKeysWithHandles(values, frames, {
              channel,
              boneName: bone.name,
              maxFrame,
              detail: options.detail,
              walking: options.walking
            })
          });
        }
      }

      return curves;
    },

    autoKeyCurveKeysWithHandles(values, frames, options = {}) {
      const detail = THREE.MathUtils.clamp(Number(options.detail) || 0, 0, 1);
      const handleStrength = THREE.MathUtils.clamp(0.04 + Math.pow(detail, 1.7) * 0.96, 0, 1);
      return frames.map((frame) => {
        const key = { frame, value: values[frame] };
        const slopes = this.autoKeyRawSlopesForFrame(values, frame, options.maxFrame);
        const contactScale = this.autoKeyContactHandleScale(options.boneName, options.channel, frame, {
          walking: options.walking,
          detail
        });
        if (Number.isFinite(slopes.inSlope)) {
          key.inSlope = slopes.inSlope * handleStrength * contactScale;
        }
        if (Number.isFinite(slopes.outSlope)) {
          key.outSlope = slopes.outSlope * handleStrength * contactScale;
        }
        return key;
      });
    },

    autoKeyContactHandleScale(boneName, channel, frame, options = {}) {
      const walking = options.walking?.active ? options.walking : null;
      if (!walking) {
        return 1;
      }
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      const isHipsPosition = boneName === walking.hipsName && POSITION_CHANNELS.has(channel);
      const isContact = walking.contactBoneNames?.has(boneName);
      const isLegRotation = walking.managedBoneNames?.has(boneName)
        && ROTATION_CHANNELS.has(channel)
        && LEG_BONE_PATTERN.test(this.autoKeyBoneSearchText(boneName));
      if (!isHipsPosition && !isContact && !isLegRotation) {
        return 1;
      }
      const side = this.autoKeyWalkingSideForBone(walking, boneName);
      const holdFrames = isHipsPosition
        ? walking.contactHoldFrames
        : side?.contact?.holdFrames || walking.contactHoldFrames;
      const denseFrames = isHipsPosition
        ? walking.denseFrames
        : side?.contact?.denseFrames || walking.denseFrames;
      const balanceFrames = isHipsPosition
        ? walking.balanceFrames
        : side?.contact?.balanceFrames || walking.balanceFrames;
      const radius = detail < 0.2 ? 1 : 0;
      const onContactAnchor = this.autoKeyFrameSetHasNearby(holdFrames, frame, radius)
        || this.autoKeyFrameSetHasNearby(denseFrames, frame, 0)
        || this.autoKeyFrameSetHasNearby(balanceFrames, frame, radius);
      if (!onContactAnchor) {
        return 1;
      }
      if (isContact) {
        return THREE.MathUtils.lerp(0.03, 0.45, detail);
      }
      if (isHipsPosition) {
        return THREE.MathUtils.lerp(0.12, 0.65, detail);
      }
      return THREE.MathUtils.lerp(0.18, 0.75, detail);
    },

    autoKeyRawSlopesForFrame(values, frame, maxFrame = values.length - 1) {
      const targetFrame = Math.max(0, Math.min(maxFrame, Math.round(frame)));
      const previous = Math.max(0, targetFrame - 1);
      const next = Math.min(maxFrame, targetFrame + 1);
      if (previous === next) {
        return {};
      }
      const slope = (finitePoseValue(values[next]) - finitePoseValue(values[previous])) / Math.max(1, next - previous);
      return {
        ...(targetFrame > 0 ? { inSlope: slope } : {}),
        ...(targetFrame < maxFrame ? { outSlope: slope } : {})
      };
    },

    autoKeyChannelInfo(values, channel, boneName = "", options = {}) {
      const walking = options.walking?.active ? options.walking : null;
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      if (!this.autoKeyShouldIncludeBoneForDetail(boneName, detail, walking)) {
        return {
          include: false,
          movementThreshold: Number.POSITIVE_INFINITY,
          offsetThreshold: Number.POSITIVE_INFINITY,
          range: 0,
          maxAbs: 0
        };
      }
      const min = Math.min(...values);
      const max = Math.max(...values);
      const range = max - min;
      const maxAbs = Math.max(...values.map((value) => Math.abs(value)));
      const isRotation = ROTATION_CHANNELS.has(channel);
      const isRoot = /hips|pelvis|root/i.test(boneName);
      const walkingManaged = walking?.managedBoneNames?.has(boneName);
      const walkingContact = walking?.contactBoneNames?.has(boneName);
      const movementThreshold = isRotation
        ? walkingManaged ? 0.0025 : 0.006
        : isRoot ? walking ? 0.004 : 0.08
          : walkingManaged ? 0.003 : 0.006;
      const offsetThreshold = isRotation
        ? walkingManaged ? 0.0025 : 0.006
        : isRoot ? walking ? 0.004 : 0.08
          : walkingContact ? 0.003 : 0.006;
      return {
        include: range > movementThreshold || maxAbs > offsetThreshold,
        movementThreshold,
        offsetThreshold,
        range,
        maxAbs
      };
    },

    autoKeyCurveTolerance(channel, boneName, info, options = {}) {
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      const toleranceScale = THREE.MathUtils.lerp(2.1, 0.025, Math.pow(detail, 1.7));
      const contactScale = CONTACT_BONE_PATTERN.test(boneName || "") ? THREE.MathUtils.lerp(0.7, 0.25, detail) : 1;
      if (ROTATION_CHANNELS.has(channel)) {
        return Math.max(0.0025, info.range * 0.2 * toleranceScale * contactScale);
      }
      const isRoot = /hips|pelvis|root/i.test(boneName);
      return isRoot
        ? Math.max(0.004, info.range * 0.035 * toleranceScale)
        : Math.max(0.0025, info.range * 0.18 * toleranceScale * contactScale);
    },

    autoKeyWalkingFramesForCurve(options = {}) {
      const walking = options.walking?.active ? options.walking : null;
      const boneName = options.boneName || "";
      if (!walking?.managedBoneNames?.has(boneName)) {
        return [];
      }
      const channel = options.channel || "";
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      const isHips = boneName === walking.hipsName;
      const isContact = walking.contactBoneNames?.has(boneName);
      const isLeg = LEG_BONE_PATTERN.test(this.autoKeyBoneSearchText(boneName));
      const side = isHips ? null : this.autoKeyWalkingSideForBone(walking, boneName);
      const transitionFrames = side?.contact?.transitionFrames || walking.transitionFrames || [];
      const denseFrames = side?.contact?.denseFrames || walking.denseFrames || [];
      const balanceFrames = isHips ? walking.balanceFrames : side?.contact?.balanceFrames || walking.balanceFrames || [];
      const frames = new Set(transitionFrames);
      const shouldUseBalance = isHips
        || isContact
        || (isLeg && ROTATION_CHANNELS.has(channel));
      if (shouldUseBalance) {
        for (const frame of balanceFrames) {
          frames.add(frame);
        }
      }
      const shouldUseSupportSync = !isHips
        && side?.contact?.windows?.length
        && (isContact || (isLeg && ROTATION_CHANNELS.has(channel)));
      if (shouldUseSupportSync) {
        for (const frame of this.autoKeySupportSyncFramesForContact(side.contact, walking, { detail })) {
          frames.add(frame);
        }
      }
      const shouldUseDenseContact = isContact
        || (isLeg && ROTATION_CHANNELS.has(channel))
        || (isHips && POSITION_CHANNELS.has(channel));
      if (shouldUseDenseContact && detail > 0.08) {
        for (const frame of denseFrames) {
          frames.add(frame);
        }
      }
      return [...frames];
    },

    autoKeySupportSyncFramesForContact(contact, walking, options = {}) {
      if (!contact?.windows?.length || !walking?.active) {
        return [];
      }
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      const frames = new Set();
      const sourceSets = [
        walking.transitionFrames,
        walking.balanceFrames,
        detail > 0.08 ? walking.denseFrames : null
      ].filter(Boolean);
      for (const sourceSet of sourceSets) {
        for (const frame of sourceSet) {
          if (this.autoKeyContactCoversFrame(contact, frame)) {
            frames.add(frame);
          }
        }
      }
      return [...frames];
    },

    autoKeyContactCoversFrame(contact, frame) {
      if (!contact?.windows?.length || !Number.isFinite(frame)) {
        return false;
      }
      const targetFrame = Math.round(frame);
      return contact.windows.some((window) => targetFrame >= window.start && targetFrame <= window.end);
    },

    autoKeyWalkingSideForBone(walking, boneName) {
      if (!walking?.sideSignals?.length || !boneName) {
        return null;
      }
      const direct = walking.sideSignals.find((side) => [
        side.upperName,
        side.lowerName,
        side.footName,
        side.toeName
      ].includes(boneName));
      if (direct) {
        return direct;
      }
      const search = this.autoKeyBoneSearchText(boneName);
      return walking.sideSignals.find((side) => new RegExp(`\\b${side.side}\\b`, "i").test(search)) || null;
    },

    autoKeyFrameSetHasNearby(frames, frame, radius = 0) {
      if (!frames?.size) {
        return false;
      }
      const targetFrame = Math.round(frame);
      const checkRadius = Math.max(0, Math.round(radius));
      for (let offset = -checkRadius; offset <= checkRadius; offset += 1) {
        if (frames.has(targetFrame + offset)) {
          return true;
        }
      }
      return false;
    },

    autoKeySelectCurveFrames(values, options = {}) {
      const maxFrame = Math.max(1, Math.floor(Number(options.maxFrame) || values.length - 1));
      const maxCurveKeys = Math.max(4, Math.floor(Number(options.maxCurveKeys) || 12));
      const info = options.info || this.autoKeyChannelInfo(values, options.channel, options.boneName);
      const detail = THREE.MathUtils.clamp(
        Number.isFinite(Number(options.detail)) ? Number(options.detail) : this.solvedKeyDetailValue(),
        0,
        1
      );
      const selected = new Set([
        0,
        maxFrame,
        ...this.autoKeyWalkingFramesForCurve({ ...options, maxFrame, detail })
      ]);

      if (info.range <= info.movementThreshold) {
        return info.maxAbs > info.offsetThreshold
          ? [...selected].sort((a, b) => a - b)
          : [0];
      }

      if (detail >= 0.995) {
        return Array.from({ length: maxFrame + 1 }, (_, frame) => frame);
      }

      this.autoKeyAddCurveExtrema(values, selected, maxFrame, maxCurveKeys);
      if (selected.size < maxCurveKeys) {
        this.autoKeyAddLargestCurveError(values, selected, maxFrame);
      }

      const tolerance = this.autoKeyCurveTolerance(options.channel, options.boneName, info, options);
      while (selected.size < maxCurveKeys) {
        const frames = [...selected].sort((a, b) => a - b);
        let best = { frame: -1, error: 0 };
        for (let index = 0; index < frames.length - 1; index += 1) {
          const start = frames[index];
          const end = frames[index + 1];
          if (end - start <= 1) {
            continue;
          }
          const startValue = values[start];
          const endValue = values[end];
          for (let frame = start + 1; frame < end; frame += 1) {
            const t = (frame - start) / Math.max(1, end - start);
            const expected = startValue + (endValue - startValue) * t;
            const error = Math.abs(values[frame] - expected);
            if (error > best.error) {
              best = { frame, error };
            }
          }
        }
        if (best.frame < 0 || best.error <= tolerance) {
          break;
        }
        selected.add(best.frame);
      }

      return [...selected].sort((a, b) => a - b);
    },

    autoKeyAddCurveExtrema(values, selected, maxFrame, maxCurveKeys = values.length) {
      let min = { frame: -1, value: Number.POSITIVE_INFINITY };
      let max = { frame: -1, value: Number.NEGATIVE_INFINITY };
      for (let frame = 1; frame < maxFrame; frame += 1) {
        const value = finitePoseValue(values[frame]);
        if (value < min.value) {
          min = { frame, value };
        }
        if (value > max.value) {
          max = { frame, value };
        }
      }
      const endpoints = [finitePoseValue(values[0]), finitePoseValue(values[maxFrame])];
      const endpointMin = Math.min(...endpoints);
      const endpointMax = Math.max(...endpoints);
      const candidates = [min, max]
        .filter((item) => item.frame > 0 && item.frame < maxFrame)
        .map((item) => ({
          frame: item.frame,
          score: Math.max(Math.abs(item.value - endpointMin), Math.abs(item.value - endpointMax))
        }))
        .sort((a, b) => b.score - a.score);
      for (const candidate of candidates) {
        if (selected.size >= maxCurveKeys) {
          break;
        }
        selected.add(candidate.frame);
      }
    },

    autoKeyAddLargestCurveError(values, selected, maxFrame) {
      if (selected.size >= maxFrame + 1) {
        return false;
      }
      const frames = [...selected].sort((a, b) => a - b);
      let best = { frame: -1, error: 0 };
      for (let index = 0; index < frames.length - 1; index += 1) {
        const start = frames[index];
        const end = frames[index + 1];
        if (end - start <= 1) {
          continue;
        }
        const startValue = values[start];
        const endValue = values[end];
        for (let frame = start + 1; frame < end; frame += 1) {
          const t = (frame - start) / Math.max(1, end - start);
          const expected = startValue + (endValue - startValue) * t;
          const error = Math.abs(values[frame] - expected);
          if (error > best.error) {
            best = { frame, error };
          }
        }
      }
      if (best.frame < 0) {
        return false;
      }
      selected.add(best.frame);
      return true;
    },

    autoKeyMotionCandidates(samples, bones, maxFrame) {
      const candidates = new Map();
      const speeds = [0];
      for (let frame = 1; frame <= maxFrame; frame += 1) {
        speeds[frame] = this.autoKeyPoseDistance(samples[frame - 1], samples[frame], bones);
      }
      const maxSpeed = Math.max(...speeds);
      const averageSpeed = speeds.reduce((sum, value) => sum + value, 0) / Math.max(1, speeds.length - 1);
      const threshold = Math.max(0.0015, maxSpeed * 0.08, averageSpeed * 0.4);

      for (let frame = 1; frame < maxFrame; frame += 1) {
        const previous = speeds[frame - 1] || 0;
        const current = speeds[frame] || 0;
        const next = speeds[frame + 1] || 0;
        if (current >= threshold && previous < threshold * 0.7) {
          addCandidate(candidates, frame - 1, current * 1.4, "start", maxFrame);
          addCandidate(candidates, frame, current * 1.2, "start", maxFrame);
        }
        if (current < threshold * 0.7 && previous >= threshold) {
          addCandidate(candidates, frame, previous * 1.3, "stop", maxFrame);
        }
        if (current < previous * 0.55 && current < next * 0.55 && Math.max(previous, next) > threshold) {
          addCandidate(candidates, frame, Math.max(previous, next), "slow", maxFrame);
        }
        const accel = Math.abs(next - current);
        const previousAccel = Math.abs(current - previous);
        if (accel > previousAccel && accel > threshold * 0.65) {
          addCandidate(candidates, frame, accel, "turn", maxFrame);
        }
      }

      for (const bone of bones) {
        for (const channel of CURVE_CHANNEL_KEYS) {
          const values = samples.map((sample) => finitePoseValue(sample?.[bone.name]?.[channel]));
          const range = Math.max(...values) - Math.min(...values);
          const channelThreshold = Math.max(0.0004, range * 0.015);
          if (range <= channelThreshold * 2) {
            continue;
          }
          for (let frame = 1; frame < maxFrame; frame += 1) {
            const before = values[frame] - values[frame - 1];
            const after = values[frame + 1] - values[frame];
            if (Math.abs(before) < channelThreshold && Math.abs(after) < channelThreshold) {
              continue;
            }
            if (Math.sign(before) !== Math.sign(after)) {
              addCandidate(candidates, frame, Math.abs(before - after) * channelWeight(channel, bone.name), "extreme", maxFrame);
            }
          }
        }
      }
      return candidates;
    },

    autoKeySelectFrames(samples, bones, candidates, options) {
      const { maxFrame, maxKeys, minKeys } = options;
      const selected = new Set([0, maxFrame]);
      const targetError = Math.max(0.012, this.autoKeyTotalMotion(samples, bones) * 0.018);

      while (selected.size < maxKeys) {
        const frames = [...selected].sort((a, b) => a - b);
        let best = { frame: -1, error: 0 };
        for (let index = 0; index < frames.length - 1; index += 1) {
          const start = frames[index];
          const end = frames[index + 1];
          if (end - start <= 1) {
            continue;
          }
          for (let frame = start + 1; frame < end; frame += 1) {
            const t = (frame - start) / Math.max(1, end - start);
            const error = this.autoKeyLinearPoseError(samples[start], samples[end], samples[frame], bones, t);
            if (error > best.error) {
              best = { frame, error };
            }
          }
        }
        if (best.frame < 0 || (best.error < targetError && selected.size >= minKeys)) {
          break;
        }
        selected.add(best.frame);
      }

      const rankedCandidates = [...candidates.values()].sort((a, b) => b.score - a.score);
      for (const candidate of rankedCandidates) {
        if (selected.size >= maxKeys) {
          break;
        }
        selected.add(candidate.frame);
      }

      for (let slot = 1; selected.size < minKeys && slot < minKeys - 1; slot += 1) {
        selected.add(Math.round((slot / Math.max(1, minKeys - 1)) * maxFrame));
      }
      while (selected.size < minKeys && selected.size < maxFrame + 1) {
        const frames = [...selected].sort((a, b) => a - b);
        let widest = { frame: -1, span: 0 };
        for (let index = 0; index < frames.length - 1; index += 1) {
          const span = frames[index + 1] - frames[index];
          if (span > widest.span) {
            widest = { frame: Math.round((frames[index] + frames[index + 1]) / 2), span };
          }
        }
        if (widest.frame < 0 || selected.has(widest.frame)) {
          break;
        }
        selected.add(widest.frame);
      }

      return [...selected].sort((a, b) => a - b);
    },

    autoKeyPoseDistance(a, b, bones) {
      let sum = 0;
      for (const bone of bones) {
        for (const channel of CURVE_CHANNEL_KEYS) {
          const delta = finitePoseValue(b?.[bone.name]?.[channel]) - finitePoseValue(a?.[bone.name]?.[channel]);
          const weight = channelWeight(channel, bone.name);
          sum += delta * delta * weight * weight;
        }
      }
      return Math.sqrt(sum);
    },

    autoKeyTotalMotion(samples, bones) {
      let total = 0;
      for (let frame = 1; frame < samples.length; frame += 1) {
        total += this.autoKeyPoseDistance(samples[frame - 1], samples[frame], bones);
      }
      return total;
    },

    autoKeyLinearPoseError(start, end, sample, bones, t) {
      let maxError = 0;
      for (const bone of bones) {
        for (const channel of CURVE_CHANNEL_KEYS) {
          const a = finitePoseValue(start?.[bone.name]?.[channel]);
          const b = finitePoseValue(end?.[bone.name]?.[channel]);
          const expected = a + (b - a) * t;
          const actual = finitePoseValue(sample?.[bone.name]?.[channel]);
          const error = Math.abs(actual - expected) * channelWeight(channel, bone.name);
          maxError = Math.max(maxError, error);
        }
      }
      return maxError;
    }
  });
}
